def _get_cwe_set_from_xml(filepath: Path) -> set[str]:
    """Parse CWE XML files and return a set of CWE string IDs."""
    if not filepath.exists():
        logger.warning(f"Could not find CWE XML at {filepath}")
        return set()
    try:
        content = filepath.read_text(encoding="utf-8")
        cwes = re.findall(r'<Weakness [^>]*ID="(\d+)"', content)
        return set(f"CWE-{x}" for x in cwes)
    except Exception as e:
        logger.warning(f"Failed to parse {filepath}: {e}")
        return set()

def _filter_suffix(cwe_list: list[str] | None, cwe_groups: list[str] | None, filter_owasp_top10: bool = False, filter_top25: bool = False) -> str:
    if not cwe_list and not cwe_groups and not filter_owasp_top10 and not filter_top25:
        return ""
    key = json.dumps(
        {"l": sorted(cwe_list or []), "g": sorted(cwe_groups or []), "owasp": filter_owasp_top10, "top25": filter_top25},
        sort_keys=True,
    )
    return "_f" + hashlib.md5(key.encode()).hexdigest()[:8]


def _parse_cwe_string(raw: str | list) -> str:
    """
    Extract the primary (first) CWE string from potentially multi-label 
    or raw list strings like "CWE-119, CWE-787" -> "CWE-119".
    Also handles lowercase and leading zeros (e.g., "cwe-089" -> "CWE-89").
    """
    if not raw:
        return ""
    if isinstance(raw, list):
        raw = raw[0] if raw else ""
    s = str(raw).strip().upper()
    if not s or s in ("NAN", "NONE", "UNKNOWN", "OTHER", "CWE-OTHER", "CWE-UNKNOWN", "NVD-CWE-NOINFO", "NVD-CWE-OTHER"):
        return ""
    if "," in s:
        s = s.split(",")[0].strip()
    
    if s.startswith("CWE-"):
        num = s[4:]
        if num.isdigit():
            return f"CWE-{int(num)}"
        return s
    
    if s.isdigit():
        return f"CWE-{int(s)}"
        
    return s


def _subsample(entries: list, max_per_class: int, seed: int) -> list:
    """Return at most max_per_class entries, sampled reproducibly."""
    if max_per_class <= 0 or len(entries) <= max_per_class:
        return entries
    rng = random.Random(seed)
    return rng.sample(entries, max_per_class)


def _streaming_collate_cache_files(
    cache_files: list[Path],
    class_names: list[str] | None,
    out_path: Path,
) -> int:
    """
    Two-pass memory-efficient merge of per-class .pt cache files into the
    PyG InMemoryDataset (data, slices, class_names) format.

    Pass 1 — scan:  load each class, record per-graph tensor sizes, delete.
    Pass 2 — fill:  pre-allocate output tensors, copy class-by-class, delete.

    Peak RAM ≈ output tensors + one class batch instead of 2× output.
    Returns total number of graphs written.
    """
    # ------------------------------------------------------------------
    # Pass 1 — scan sizes and dtypes
    # ------------------------------------------------------------------
    logger.info("Streaming merge — pass 1: scanning sizes…")

    sizes: dict[str, list[int]] = {}       # per-graph size along cat_dim
    cat_dims: dict[str, int] = {}
    dtypes: dict[str, torch.dtype] = {}
    example_shapes: dict[str, list[int]] = {}
    raw_funcs: list[str] = []
    n_graphs = 0

    for cache_file in tqdm(cache_files, desc="  pass-1 scan", unit="class"):
        if not cache_file.exists():
            logger.warning(f"  Missing cache file: {cache_file} — skipping")
            continue
        cls_graphs: list[Data] = torch.load(cache_file, weights_only=False)
        for g in cls_graphs:
            n_graphs += 1
            raw_funcs.append(getattr(g, "raw_func", "") or "")
            for key in g.keys():
                val = g[key]
                if not isinstance(val, torch.Tensor):
                    continue
                cat_dim = g.__cat_dim__(key, val)
                if key not in sizes:
                    sizes[key] = []
                    cat_dims[key] = cat_dim
                    dtypes[key] = val.dtype
                    example_shapes[key] = list(val.shape)
                sizes[key].append(val.shape[cat_dim])
        del cls_graphs
        gc.collect()

    if n_graphs == 0:
        raise RuntimeError("No graphs found in cache files.")

    logger.info(f"  Scanned {n_graphs} graphs, {len(sizes)} attributes")

    # Build cumulative slices
    slices: dict[str, torch.Tensor] = {}
    for key, sz_list in sizes.items():
        cum = torch.zeros(len(sz_list) + 1, dtype=torch.long)
        for i, s in enumerate(sz_list):
            cum[i + 1] = cum[i] + s
        slices[key] = cum

    # ------------------------------------------------------------------
    # Pre-allocate output tensors
    # ------------------------------------------------------------------
    logger.info("  Pre-allocating output tensors…")
    out_data = Data()
    for key in sizes:
        total = int(slices[key][-1])
        shape = list(example_shapes[key])
        shape[cat_dims[key]] = total
        out_data[key] = torch.empty(shape, dtype=dtypes[key])

    has_x = "x" in slices

    # ------------------------------------------------------------------
    # Pass 2 — fill pre-allocated tensors
    # ------------------------------------------------------------------
    logger.info("Streaming merge — pass 2: filling tensors…")

    write_offs: dict[str, int] = {k: 0 for k in sizes}
    graph_idx = 0

    for cache_file in tqdm(cache_files, desc="  pass-2 fill", unit="class"):
        if not cache_file.exists():
            continue
        cls_graphs: list[Data] = torch.load(cache_file, weights_only=False)
        for g in cls_graphs:
            node_off = int(slices["x"][graph_idx]) if has_x else 0
            for key in sizes:
                val = g[key]
                if val is None or not isinstance(val, torch.Tensor):
                    continue
                cat_dim = cat_dims[key]
                sz = val.shape[cat_dim]
                off = write_offs[key]
                slc: list = [slice(None)] * val.dim()
                slc[cat_dim] = slice(off, off + sz)
                if key == "edge_index" and has_x:
                    out_data[key][tuple(slc)] = val + node_off
                else:
                    out_data[key][tuple(slc)] = val
                write_offs[key] += sz
            graph_idx += 1
        del cls_graphs
        gc.collect()

    logger.info(f"  Saving {n_graphs} graphs → {out_path}")
    torch.save((out_data, slices, class_names, raw_funcs), out_path)
    logger.info("  Saved.")
    return n_graphs


class CodeBERTGraphDataset(InMemoryDataset):
    """
    Dataset of CPG graphs with CodeBERT node embeddings (773D per node, 7D edge features).

    Parameters
    ----------
    root : str | Path
        Root data directory.
    max_nodes : int
        Graphs with more nodes than this are skipped.
    embedder_device : str
        Device used by CodeBERT during preprocessing.
    mode : str
        "binary", "multiclass", or "group".
    source : str
        Subdirectory under raw/ (e.g. "bigvul").
    pretrained_lm : str
        HuggingFace model ID for node embeddings.
    add_func_tokens : bool
        Tokenize full function text and store in Data.
    top_cwe : int
        Keep only top-N CWE classes by vocab index (0 = all).
    cwe_list : list[str] | None
        Explicit CWE whitelist for vulnerable samples.
    cwe_groups : list[str] | None
        Group whitelist — expanded to all CWEs in those groups.
    max_per_class : int
        Maximum graphs per work-unit (class/CWE/group bucket). 0 = no limit.
        Applied before embedding so only sampled graphs are processed.
        Different values produce separate cached .pt files.
    resample_seed : int
        Random seed for max_per_class sampling. Change to get a different sample.
    """

    def __init__(
        self,
        root: str | Path = "data",
        max_nodes: int = 500,
        embedder_device: str = "cpu",
        mode: str = "binary",
        source: str = "bigvul",
        pretrained_lm: str = "microsoft/codebert-base",
        func_lm: str = "",
        add_func_tokens: bool = False,
        func_lm_source: str = "raw",   # "raw" | "normalized" — source text for func_lm tokenization
        top_cwe: int = 0,
        cwe_list: list[str] | None = None,
        cwe_groups: list[str] | None = None,
        filter_owasp_top10: bool = False,
        filter_top25: bool = False,
        max_per_class: int = 0,
        resample_seed: int = 42,
        force_rebuild: bool = False,
        transform=None,
        pre_transform=None,
    ):
        self._force_rebuild = force_rebuild
        self.max_nodes = max_nodes
        self._embedder_device = embedder_device
        self._pretrained_lm = pretrained_lm
        self._add_func_tokens = add_func_tokens
        self._func_lm_source = func_lm_source
        self._source = source
        self._top_cwe = top_cwe
        self._cwe_list = list(cwe_list) if cwe_list else []
        self._cwe_groups = cwe_groups
        self._filter_owasp_top10 = filter_owasp_top10
        self._filter_top25 = filter_top25
        self._max_per_class = max_per_class
        self._resample_seed = resample_seed
        self._lm_short = pretrained_lm.split("/")[-1]
        self._func_lm = func_lm if func_lm else pretrained_lm
        self._func_short = self._func_lm.split("/")[-1]

        # Load dynamic XML filters
        cwe_dir = Path(root).parent / "data" / "cwe"
        if filter_owasp_top10:
            self._cwe_list.extend(_get_cwe_set_from_xml(cwe_dir / "owasptop10.xml"))
        if filter_top25:
            self._cwe_list.extend(_get_cwe_set_from_xml(cwe_dir / "top25.xml"))

        # Compute effective CWE filter set (None = no filter)
        self._effective_cwes = _expand_cwe_filter(self._cwe_list if self._cwe_list else None, cwe_groups)
        self._fsuffix = _filter_suffix(self._cwe_list if self._cwe_list else None, cwe_groups, filter_owasp_top10, filter_top25)

        source_dir = Path(root) / "raw" / source
        has_vocab = (source_dir / "cwe_vocab.json").exists()
        if mode in ("multiclass", "group") and not has_vocab:
            raise RuntimeError(
                f"mode='{mode}' but cwe_vocab.json not found under {source_dir}. "
                "Run prepare_dataset.py --top-cwe N to generate it."
            )
        self._mode = mode

        super().__init__(str(root), transform, pre_transform)

        result = torch.load(self.processed_paths[0], weights_only=False)
        if len(result) == 4:
            self.data, self.slices, self.class_names, self.raw_funcs = result
        elif len(result) == 3:
            self.data, self.slices, self.class_names = result
            self.raw_funcs = None
        else:
            self.data, self.slices = result
            self.class_names = None
            self.raw_funcs = None

    # ------------------------------------------------------------------
    # PyG hooks
    # ------------------------------------------------------------------

    @property
    def raw_file_names(self) -> list[str]:
        return []

    @property
    def processed_file_names(self) -> list[str]:
        ft_suffix = "_ft" if self._add_func_tokens else ""
        top_suffix = f"_top{self._top_cwe}" if self._top_cwe > 0 else ""
        samp_suffix = (
            f"_s{self._max_per_class}r{self._resample_seed}"
            if self._max_per_class > 0 else ""
        )
        live_suffix = f"_live_{self._func_short}" if self._func_short != self._lm_short else ""
        return [
            f"lm_dataset_{self._source}_{self._mode}_{self._lm_short}{live_suffix}"
            f"{ft_suffix}{top_suffix}{self._fsuffix}{samp_suffix}.pt"
        ]

    def download(self) -> None:
        pass

    # ------------------------------------------------------------------
    # Patch fast-path: re-tokenize existing .pt when only func_lm changed
    # ------------------------------------------------------------------

    def _try_patch_from_existing(self, out_path: Path) -> bool:
        """
        Find a compatible processed .pt (same config, different func_lm) that
        contains raw_funcs, re-tokenize with the new func_lm, and save as
        out_path. Skips the expensive CodeBERT node-embedding step entirely.

        Returns True if patched successfully, False if no compatible base found.
        """
        if not self._add_func_tokens or self._force_rebuild:
            return False

        ft_suffix = "_ft"
        top_suffix = f"_top{self._top_cwe}" if self._top_cwe > 0 else ""
        samp_suffix = (
            f"_s{self._max_per_class}r{self._resample_seed}"
            if self._max_per_class > 0 else ""
        )
        prefix = f"lm_dataset_{self._source}_{self._mode}_{self._lm_short}"
        suffix = f"{ft_suffix}{top_suffix}{self._fsuffix}{samp_suffix}.pt"

        candidates = [
            p for p in out_path.parent.glob(f"{prefix}*.pt")
            if p != out_path and p.name.endswith(suffix)
        ]
        if not candidates:
            return False

        base_pt = candidates[0]
        logger.info(f"Patch fast-path: compatible base found → {base_pt.name}")

        result = torch.load(base_pt, weights_only=False)
        if len(result) < 4 or not result[3]:
            logger.warning("Base .pt has no raw_funcs — falling back to full build")
            return False

        data, slices, class_names, raw_funcs = result
        n = len(raw_funcs)
        logger.info(f"  Re-tokenizing {n} functions with {self._func_lm} …")

        from transformers import AutoTokenizer
        tokenizer = AutoTokenizer.from_pretrained(self._func_lm, trust_remote_code=True)

        ids_list, mask_list = [], []
        batch_sz = 64
        for i in tqdm(range(0, n, batch_sz), desc="  patch tokenize", unit="batch"):
            enc = tokenizer(
                raw_funcs[i : i + batch_sz],
                max_length=512, truncation=True,
                padding="max_length", return_tensors="pt",
            )
            ids_list.append(enc["input_ids"])
            mask_list.append(enc["attention_mask"])

        data.func_input_ids = torch.cat(ids_list, dim=0)       # [n, 512]
        data.func_attention_mask = torch.cat(mask_list, dim=0) # [n, 512]
        # slices unchanged — same tensor shape [n, 512]

        logger.info(f"  Saving patched .pt → {out_path}")
        torch.save((data, slices, class_names, raw_funcs), out_path)
        logger.info("  Patch complete.")
        return True

    def process(self) -> None:
        if self._try_patch_from_existing(Path(self.processed_paths[0])):
            return

        source_dir = Path(self.raw_dir) / self._source
        if not source_dir.is_dir():
            raise RuntimeError(
                f"Source directory not found: {source_dir}. "
                f"Run prepare_dataset.py --out-dir data/raw --format {self._source} first."
            )

        vocab_path = source_dir / "cwe_vocab.json"
        class_sources: list[tuple[str, bool, list[Path]]] = []
        for cls_name in ("benign", "vulnerable"):
            cls_dir = source_dir / cls_name
            if cls_dir.is_dir():
                files = sorted(
                    f for f in cls_dir.iterdir()
                    if f.suffix.lower() in (".json", ".xml", ".graphml")
                    and ".meta." not in f.name
                )
                class_sources.append((cls_name, cls_name == "benign", files))
                logger.info(f"Source [{self._source}/{cls_name}]: {len(files)} CPG files")

        if not class_sources:
            raise RuntimeError(
                f"No CPG files found under {source_dir}. "
                "Expected benign/ and/or vulnerable/ subdirectories."
            )

        # ------------------------------------------------------------------
        # Load vocabulary
        # ------------------------------------------------------------------
        is_multi = self._mode in ("multiclass", "group")
        if is_multi:
            with open(vocab_path, encoding="utf-8") as f:
                cwe_vocab: dict[str, int] = json.load(f)
            if self._top_cwe > 0:
                cwe_vocab = {k: v for k, v in cwe_vocab.items() if v <= self._top_cwe}
        else:
            cwe_vocab = {}

        # Apply cwe_list/cwe_groups filter to vocab (for multiclass/group)
        if is_multi and self._effective_cwes is not None:
            cwe_vocab = {
                k: v for k, v in cwe_vocab.items()
                if k == "benign" or k in self._effective_cwes
            }

        id_to_cwe: dict[int, str] = {v: k for k, v in cwe_vocab.items()}

        # ------------------------------------------------------------------
        # Build work units
        # Entry format: (path, label, flaw_lines, cwe_str, raw_func, row_id)
        # ------------------------------------------------------------------
        # work_units: list of (cache_key, entries_list)
        work_units: list[tuple[str, list[tuple[Path, int, list[int], str, str, int]]]] = []

        for cls_name, is_benign_dir, cpg_files in class_sources:
            base_label = 0 if is_benign_dir else 1

            if is_benign_dir:
                # Benign: always include, no CWE filter
                entries: list[tuple[Path, int, list[int], str, str, int]] = []
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    flaw_lines: list[int] = []
                    raw_func = ""
                    row_id = -1
                    if meta_path.exists():
                        with open(meta_path) as mf:
                            _m = json.load(mf)
                        flaw_lines = _m.get("flaw_lines", [])
                        raw_func = _m.get("raw_func", "")
                        row_id = _m.get("id", -1)
                    entries.append((gf, base_label, flaw_lines, "", raw_func, row_id))
                work_units.append((cls_name, entries))

            elif not is_multi:
                # Binary vulnerable: may still filter by CWE if cwe_list/cwe_groups given
                entries = []
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    flaw_lines = []
                    cwe_str = ""
                    raw_func = ""
                    row_id = -1
                    if meta_path.exists():
                        with open(meta_path) as mf:
                            meta = json.load(mf)
                        flaw_lines = meta.get("flaw_lines", [])
                        cwe_str = _parse_cwe_string(meta.get("cwe", ""))
                        raw_func = meta.get("raw_func", "")
                        row_id = meta.get("id", -1)
                    if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                        continue
                    entries.append((gf, base_label, flaw_lines, cwe_str, raw_func, row_id))
                work_units.append((cls_name, entries))

            elif self._mode == "multiclass":
                # Group by CWE class_id → one cache file per CWE
                logger.info(f"  [{cls_name}] grouping {len(cpg_files)} files by CWE class…")
                cwe_class_groups: dict[int, list[tuple[Path, int, list[int], str, str]]] = {}
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    if not meta_path.exists():
                        continue
                    with open(meta_path) as mf:
                        meta = json.load(mf)
                    cwe_str = _parse_cwe_string(meta.get("cwe", ""))
                    if cwe_str and cwe_str in cwe_vocab:
                        class_id = cwe_vocab[cwe_str]
                    elif self._top_cwe > 0 or self._effective_cwes is not None:
                        class_id = -1
                    else:
                        class_id = meta.get("class_id", -1)
                    if class_id < 0:
                        continue
                    # Apply effective_cwes filter
                    if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                        continue
                    flaw_lines = meta.get("flaw_lines", [])
                    raw_func = meta.get("raw_func", "")
                    row_id = meta.get("id", -1)
                    cwe_class_groups.setdefault(class_id, []).append(
                        (gf, class_id, flaw_lines, cwe_str, raw_func, row_id)
                    )

                for class_id, grp_entries in sorted(cwe_class_groups.items()):
                    cwe_name = id_to_cwe.get(class_id, f"class_{class_id}")
                    cache_key = f"vuln_{cwe_name.replace('-', '_')}"
                    work_units.append((cache_key, grp_entries))
                    logger.info(f"    {cwe_name}: {len(grp_entries)} files → {cache_key}.pt")

            else:
                # group mode: group by vulnerability group name → one cache file per group
                logger.info(f"  [{cls_name}] grouping {len(cpg_files)} files by group…")
                grp_buckets: dict[str, list[tuple[Path, int, list[int], str, str]]] = {}
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    if not meta_path.exists():
                        continue
                    with open(meta_path) as mf:
                        meta = json.load(mf)
                    cwe_str = _parse_cwe_string(meta.get("cwe", ""))
                    group_name = CWE_GROUP_MAP.get(cwe_str, "")
                    if not group_name:
                        continue  # skip CWEs not in our group map
                    if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                        continue
                    if is_multi and cwe_str not in cwe_vocab:
                        continue
                    flaw_lines = meta.get("flaw_lines", [])
                    raw_func = meta.get("raw_func", "")
                    row_id = meta.get("id", -1)
                    grp_buckets.setdefault(group_name, []).append(
                        (gf, -1, flaw_lines, cwe_str, raw_func, row_id)  # label assigned later
                    )

                for group_name, grp_entries in sorted(grp_buckets.items()):
                    cache_key = f"vuln_{group_name}"
                    work_units.append((cache_key, grp_entries))
                    logger.info(f"    {group_name}: {len(grp_entries)} files → {cache_key}.pt")

        # ------------------------------------------------------------------
        # Build group vocab for "group" mode (contiguous remapping)
        # ------------------------------------------------------------------
        group_remap: dict[str, int] | None = None
        group_class_names: list[str] | None = None
        if self._mode == "group":
            # Collect groups present in work_units
            present_groups = sorted(
                {k.replace("vuln_", "") for k, _ in work_units if k.startswith("vuln_")}
            )
            # Contiguous: 0=benign, 1..N = groups sorted alphabetically
            group_remap = {"benign": 0}
            for i, g in enumerate(present_groups, start=1):
                group_remap[g] = i
            group_class_names = ["benign"] + present_groups
            logger.info(f"Group mode: {len(group_class_names)} classes: {group_class_names}")

        # ------------------------------------------------------------------
        # Derive class_names for multiclass mode
        # ------------------------------------------------------------------
        if self._mode == "multiclass":
            class_names: list[str] | None = [
                name for name, _ in sorted(cwe_vocab.items(), key=lambda kv: kv[1])
            ]
            logger.info(
                f"Multi-class mode: {len(class_names)} classes"
                + (f" (filtered to top-{self._top_cwe})" if self._top_cwe > 0 else "")
                + (f" + effective_cwes filter" if self._effective_cwes else "")
            )
        elif self._mode == "group":
            class_names = group_class_names
        else:
            class_names = None
            logger.info("Binary mode.")

        # ------------------------------------------------------------------
        # Apply per-class subsampling
        # ------------------------------------------------------------------
        if self._max_per_class > 0:
            sampled_units = []
            for cache_key, entries in work_units:
                orig = len(entries)
                entries = _subsample(entries, self._max_per_class, self._resample_seed)
                if orig != len(entries):
                    logger.info(
                        f"  [{cache_key}] subsampled {orig} → {len(entries)} "
                        f"(max_per_class={self._max_per_class}, seed={self._resample_seed})"
                    )
                sampled_units.append((cache_key, entries))
            work_units = sampled_units

        # ------------------------------------------------------------------
        # Cache directory
        # ------------------------------------------------------------------
        ft_suffix = "_ft" if self._add_func_tokens else ""
        src_suffix = f"_{self._func_lm_source}" if self._add_func_tokens else ""
        top_suffix = f"_top{self._top_cwe}" if self._top_cwe > 0 else ""
        samp_suffix = (
            f"_s{self._max_per_class}r{self._resample_seed}"
            if self._max_per_class > 0 else ""
        )
        cache_dir = (
            Path(self.processed_dir)
            / f"cls_cache_{self._mode}{ft_suffix}{src_suffix}{top_suffix}{self._fsuffix}{samp_suffix}"
        )
        cache_dir.mkdir(parents=True, exist_ok=True)

        done_keys    = [k for k, _ in work_units if (cache_dir / f"{k}.pt").exists()]
        pending_keys = [k for k, _ in work_units if not (cache_dir / f"{k}.pt").exists()]

        if done_keys:
            logger.info(f"Resuming — {len(done_keys)} unit(s) already cached: {done_keys}")
            logger.info(f"Remaining: {pending_keys}")

        embedder = None
        if pending_keys:
            logger.info(f"Initialising node embedder ({self._pretrained_lm})…")
            embedder = CodeBERTNodeEmbedder(
                model_name=self._pretrained_lm,
                device=self._embedder_device,
            )

        tokenizer = None
        if self._add_func_tokens and pending_keys:
            from transformers import AutoTokenizer
            logger.info(f"Initialising function tokenizer ({self._func_lm})…")
            tokenizer = AutoTokenizer.from_pretrained(self._func_lm, trust_remote_code=True)

        # ------------------------------------------------------------------
        # Process each work unit
        # ------------------------------------------------------------------
        for cache_key, entries in work_units:
            cache_file = cache_dir / f"{cache_key}.pt"

            if cache_file.exists():
                logger.info(f"  [{cache_key}] already cached — skipping")
                continue

            # Determine y label for this work unit
            is_benign_unit = cache_key == "benign"
            if is_benign_unit:
                unit_label = 0
                unit_group_name = "benign"
            elif self._mode == "group":
                group_name = cache_key.replace("vuln_", "")
                unit_label = group_remap[group_name]
                unit_group_name = group_name
            else:
                # multiclass: label is per-entry (from entries[i][1])
                unit_label = None
                unit_group_name = None

            # Phase 1: Parse
            logger.info(f"  [{cache_key}] parsing {len(entries)} CPG files…")
            parsed_entries: list[tuple[int, list[int], dict, str, str, int]] = []
            parse_skipped = 0

            for gf, label, flaw_lines, cwe_str, raw_func, row_id in tqdm(
                entries, desc=f"  {cache_key} parse", unit="graph", leave=False
            ):
                cpg = parse_cpg(gf, self.max_nodes)
                if cpg is None:
                    parse_skipped += 1
                    continue
                actual_label = unit_label if unit_label is not None else label
                parsed_entries.append((actual_label, flaw_lines, cpg, cwe_str, raw_func, row_id))

            logger.info(
                f"  [{cache_key}] parsed {len(parsed_entries)}/{len(entries)} "
                f"({parse_skipped} skipped — empty or too large)"
            )

            # Phase 2: Embed
            all_codes: list[str] = []
            offsets: list[tuple[int, int]] = []
            for _, _, cpg, _, _, _ in parsed_entries:
                start = len(all_codes)
                all_codes.extend(cpg["codes"])
                offsets.append((start, len(all_codes)))

            total_nodes = len(all_codes)
            embed_batch = 512
            total_batches = (total_nodes + embed_batch - 1) // embed_batch
            logger.info(
                f"  [{cache_key}] embedding {total_nodes} nodes "
                f"across {len(parsed_entries)} graphs ({total_batches} batches of {embed_batch})…"
            )

            cls_parts: list[torch.Tensor] = []
            for i in tqdm(
                range(0, total_nodes, embed_batch),
                desc=f"  {cache_key} embed",
                unit="batch",
                leave=True,
            ):
                cls_parts.append(embedder.embed_batch(all_codes[i : i + embed_batch]))
            all_embeddings = torch.cat(cls_parts, dim=0) if cls_parts else torch.zeros((0, 768))

            # Phase 3: Build
            cls_graphs = []
            for (label, flaw_lines, cpg, cwe_str, raw_func, row_id), (start, end) in zip(parsed_entries, offsets):
                func_input_ids = func_attention_mask = None
                if tokenizer is not None:
                    if self._func_lm_source == "normalized" and raw_func:
                        from gnn_vuln.data.preprocess import preprocess
                        func_text = preprocess(raw_func, lang="c", normalize=True)
                    elif raw_func:
                        func_text = raw_func   # "raw" — original source, line-number-safe
                    else:
                        func_text = build_func_text(cpg)  # backward compat: old meta without raw_func
                    enc = tokenizer(
                        func_text, max_length=512, truncation=True,
                        padding="max_length", return_tensors="pt",
                    )
                    func_input_ids = enc["input_ids"]
                    func_attention_mask = enc["attention_mask"]
                graph = build_from_parsed(
                    cpg, all_embeddings[start:end], label, flaw_lines,
                    func_input_ids, func_attention_mask,
                )
                # Attach CWE metadata
                raw_cwe_id = cwe_vocab.get(cwe_str, -1) if cwe_str else -1
                raw_group_name = CWE_GROUP_MAP.get(cwe_str, "") if cwe_str else ""
                raw_group_id = GROUP_VOCAB.get(raw_group_name, -1) if raw_group_name else (
                    GROUP_VOCAB["benign"] if is_benign_unit else -1
                )
                graph.cwe_id = torch.tensor([raw_cwe_id], dtype=torch.long)
                graph.group_id = torch.tensor([raw_group_id], dtype=torch.long)
                graph.parquet_id = torch.tensor([row_id], dtype=torch.long)
                graph.raw_func = raw_func or ""

                if self.pre_transform is not None:
                    graph = self.pre_transform(graph)
                cls_graphs.append(graph)

            torch.save(cls_graphs, cache_file)
            logger.info(f"  [{cache_key}] done — {len(cls_graphs)} graphs saved to cache")

        # ------------------------------------------------------------------
        # Streaming merge: two-pass collate without holding all graphs in RAM
        # ------------------------------------------------------------------
        ordered_cache_files = [cache_dir / f"{k}.pt" for k, _ in work_units]
        n = _streaming_collate_cache_files(
            ordered_cache_files, class_names, Path(self.processed_paths[0])
        )
        shutil.rmtree(cache_dir, ignore_errors=True)
        logger.info(f"Per-class cache cleaned up. Total graphs: {n}")

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    @property
    def num_classes(self) -> int:
        if self.class_names:
            return len(self.class_names)
        return int(self.data.y.max().item()) + 1

    def get_splits(
        self,
        train_ratio: float = 0.7,
        val_ratio: float = 0.15,
        seed: int = 42,
    ) -> tuple[list[int], list[int], list[int]]:
        """Return (train_idx, val_idx, test_idx) index lists."""
        import random

        indices = list(range(len(self)))
        random.seed(seed)
        random.shuffle(indices)
        n = len(indices)
        t = int(n * train_ratio)
        v = int(n * val_ratio)
        return indices[:t], indices[t : t + v], indices[t + v :]
