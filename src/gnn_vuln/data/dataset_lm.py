"""
dataset_lm.py
~~~~~~~~~~~~~
PyTorch Geometric InMemoryDataset using CodeBERT node embeddings.

Layout expected under `root/`:
    raw/
        benign/         → label 0
        vulnerable/     → label 1  (or 1..K in multi-class; read from .meta.json)
        cwe_vocab.json  → optional; enables multi-class / group mode
    processed/
        lm_dataset.pt   ← generated by this class

Modes
-----
binary      y = 0 (benign) or 1 (vulnerable)
multiclass  y = CWE class ID from cwe_vocab.json  (requires cwe_vocab.json)
group       y = contiguous group ID derived from CWE_GROUP_MAP  (requires cwe_vocab.json)

Filtering (all modes)
---------------------
top_cwe     keep only top-N CWE classes by vocab index (0 = no filter)
cwe_list    keep only CWEs in this explicit list, e.g. ["CWE-119", "CWE-787"]
cwe_groups  keep only CWEs belonging to these groups, e.g. ["memory_safety", "injection"]
            (uses CWE_GROUP_MAP; cwe_list and cwe_groups are unioned when both given)

Both cwe_list and cwe_groups apply to the *vulnerable* side only.
Benign samples are always included.

Extra tensors stored on every Data object
-----------------------------------------
cwe_id    raw CWE vocab index from cwe_vocab.json (-1 for benign / unknown CWE)
group_id  fixed GROUP_VOCAB index 0-15 (-1 for unknown CWE)

Usage
-----
    from gnn_vuln.data.dataset_lm import CodeBERTGraphDataset

    ds = CodeBERTGraphDataset(root="data", mode="group",
                               cwe_groups=["memory_safety", "injection"])
    print(ds[0])  # Data(x=[N,773], ..., cwe_id=[1], group_id=[1])
    train_idx, val_idx, test_idx = ds.get_splits()
"""

