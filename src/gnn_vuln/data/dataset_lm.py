def _fix_special_tokens_map(v):
    """Recursively convert dict-valued special tokens to their string content.
    codet5p-220m stores tokens as {'content': '...', 'single_word': False, ...}
    instead of plain strings, which breaks the tokenizers Rust backend."""
    if isinstance(v, dict):
        return v.get("content", "")
    if isinstance(v, list):
        return [_fix_special_tokens_map(x) for x in v]
    return v


def _load_tokenizer(model_name: str):
    """Load AutoTokenizer with 3-tier fallback for broken special_tokens_map.json.

    Tier 1: Normal fast tokenizer (default, fastest).
    Tier 2: Slow Python tokenizer (use_fast=False).
    Tier 3: Fix special_tokens_map.json in a temp copy of the HF cache
            and load from there. Handles codet5p-220m and similar models
            that store special tokens as dicts instead of strings.
    """
    import json
    import shutil
    import tempfile
    from transformers import AutoTokenizer

    _is_addedtoken_err = lambda e: "AddedToken" in str(e) or "Input must be" in str(e)

    # Tier 1 — fast tokenizer
    try:
        return AutoTokenizer.from_pretrained(model_name, trust_remote_code=True)
    except TypeError as e:
        if not _is_addedtoken_err(e):
            raise
        logger.warning(f"Fast tokenizer failed for {model_name} ({e}), retrying slow …")

    # Tier 2 — slow Python tokenizer
    try:
        return AutoTokenizer.from_pretrained(
            model_name, trust_remote_code=True, use_fast=False
        )
    except TypeError as e:
        if not _is_addedtoken_err(e):
            raise
        logger.warning(
            f"Slow tokenizer also failed for {model_name} ({e}). "
            "Patching special_tokens_map.json in HF cache …"
        )

    # Tier 3 — patch special_tokens_map.json: copy HF snapshot to tmp, fix dicts, load
    from huggingface_hub import snapshot_download
    cache_path = snapshot_download(model_name)
    tmp_dir = tempfile.mkdtemp(prefix="hf_tok_fix_")
    try:
        shutil.copytree(cache_path, tmp_dir, dirs_exist_ok=True)
        smap_path = Path(tmp_dir) / "special_tokens_map.json"
        if smap_path.exists():
            with open(smap_path, encoding="utf-8") as f:
                smap = json.load(f)
            fixed = {k: _fix_special_tokens_map(v) for k, v in smap.items()}
            with open(smap_path, "w", encoding="utf-8") as f:
                json.dump(fixed, f, indent=2)
            logger.info(f"Fixed special_tokens_map.json in {tmp_dir}")
        return AutoTokenizer.from_pretrained(tmp_dir, trust_remote_code=True)
    finally:
        shutil.rmtree(tmp_dir, ignore_errors=True)


def _compute_func_token_lines(func_text: str, total_len: int, tokenizer) -> torch.Tensor:
    """Map each token position to its 1-indexed source line number.
    Special tokens and padding → -1.
    Uses per-line tokenization to count tokens per line.
    """
    lines = func_text.split('\n')
    token_lines = [-1]  # CLS / BOS token
    for line_idx, line in enumerate(lines):
        try:
            ids = tokenizer(line, add_special_tokens=False)['input_ids']
        except Exception:
            ids = []
        token_lines.extend([line_idx + 1] * len(ids))  # 1-indexed to match node_line
    token_lines.append(-1)  # SEP / EOS token
    token_lines = token_lines[:total_len]   # truncate to max_length
    while len(token_lines) < total_len:     # pad with -1
        token_lines.append(-1)
    return torch.tensor(token_lines, dtype=torch.int32)


def _get_cwe_set_from_xml(filepath: Path) -> set[str]:
    """Parse CWE XML files and return a set of CWE string IDs."""
    if not filepath.exists():
        logger.warning(f"Could not find CWE XML at {filepath}")
        return set()
    try:
        content = filepath.read_text(encoding="utf-8")
        cwes = re.findall(r'<Weakness [^>]*ID="(\d+)"', content)
        return set(f"CWE-{x}" for x in cwes)
    except Exception as e:
        logger.warning(f"Failed to parse {filepath}: {e}")
        return set()

def _filter_suffix(cwe_list: list[str] | None, cwe_groups: list[str] | None, filter_owasp: bool = False, filter_top25_dangerous: bool = False) -> str:
    if not cwe_list and not cwe_groups and not filter_owasp and not filter_top25_dangerous:
        return ""
    key = json.dumps(
        {"l": sorted(cwe_list or []), "g": sorted(cwe_groups or []), "owasp": filter_owasp, "top25": filter_top25_dangerous},
        sort_keys=True,
    )
    return "_f" + hashlib.md5(key.encode()).hexdigest()[:8]


def _parse_cwe_string(raw: str | list) -> str:
    """
    Extract the primary (first) CWE string from potentially multi-label 
    or raw list strings like "CWE-119, CWE-787" -> "CWE-119".
    Also handles lowercase and leading zeros (e.g., "cwe-089" -> "CWE-89").
    """
    if not raw:
        return ""
    if isinstance(raw, list):
        raw = raw[0] if raw else ""
    s = str(raw).strip().upper()
    if not s or s in ("NAN", "NONE", "UNKNOWN", "OTHER", "CWE-OTHER", "CWE-UNKNOWN", "NVD-CWE-NOINFO", "NVD-CWE-OTHER"):
        return ""
    if "," in s:
        s = s.split(",")[0].strip()
    
    if s.startswith("CWE-"):
        num = s[4:]
        if num.isdigit():
            return f"CWE-{int(num)}"
        return s
    
    if s.isdigit():
        return f"CWE-{int(s)}"
        
    return s


def _subsample(entries: list, max_per_class: int, seed: int) -> list:
    """Return at most max_per_class entries, sampled reproducibly."""
    if max_per_class <= 0 or len(entries) <= max_per_class:
        return entries
    rng = random.Random(seed)
    return rng.sample(entries, max_per_class)


def _streaming_collate_cache_files(
    cache_files: list[Path],
    class_names: list[str] | None,
    out_path: Path,
) -> int:
    """
    Two-pass memory-efficient merge of per-class .pt cache files into the
    PyG InMemoryDataset (data, slices, class_names) format.

    Pass 1 — scan:  load each class, record per-graph tensor sizes, delete.
    Pass 2 — fill:  pre-allocate output tensors, copy class-by-class, delete.

    Peak RAM ≈ output tensors + one class batch instead of 2× output.
    Returns total number of graphs written.
    """
    # ------------------------------------------------------------------
    # Pass 1 — scan sizes and dtypes
    # ------------------------------------------------------------------
    logger.info("Streaming merge — pass 1: scanning sizes…")

    sizes: dict[str, list[int]] = {}       # per-graph size along cat_dim
    cat_dims: dict[str, int] = {}
    dtypes: dict[str, torch.dtype] = {}
    example_shapes: dict[str, list[int]] = {}
    raw_funcs: list[str] = []
    n_graphs = 0

    for cache_file in tqdm(cache_files, desc="  pass-1 scan", unit="class"):
        if not cache_file.exists():
            logger.warning(f"  Missing cache file: {cache_file} — skipping")
            continue
        cls_graphs: list[Data] = torch.load(cache_file, weights_only=False)
        for g in cls_graphs:
            n_graphs += 1
            raw_funcs.append(getattr(g, "raw_func", "") or "")
            for key in g.keys():
                val = g[key]
                if not isinstance(val, torch.Tensor):
                    continue
                cat_dim = g.__cat_dim__(key, val)
                if key not in sizes:
                    sizes[key] = []
                    cat_dims[key] = cat_dim
                    dtypes[key] = val.dtype
                    example_shapes[key] = list(val.shape)
                sizes[key].append(val.shape[cat_dim])
        del cls_graphs
        gc.collect()

    if n_graphs == 0:
        raise RuntimeError("No graphs found in cache files.")

    logger.info(f"  Scanned {n_graphs} graphs, {len(sizes)} attributes")

    # Build cumulative slices
    slices: dict[str, torch.Tensor] = {}
    for key, sz_list in sizes.items():
        cum = torch.zeros(len(sz_list) + 1, dtype=torch.long)
        for i, s in enumerate(sz_list):
            cum[i + 1] = cum[i] + s
        slices[key] = cum

    # ------------------------------------------------------------------
    # Pre-allocate output tensors
    # ------------------------------------------------------------------
    logger.info("  Pre-allocating output tensors…")
    out_data = Data()
    for key in sizes:
        total = int(slices[key][-1])
        shape = list(example_shapes[key])
        shape[cat_dims[key]] = total
        out_data[key] = torch.empty(shape, dtype=dtypes[key])

    has_x = "x" in slices

    # ------------------------------------------------------------------
    # Pass 2 — fill pre-allocated tensors
    # ------------------------------------------------------------------
    logger.info("Streaming merge — pass 2: filling tensors…")

    write_offs: dict[str, int] = {k: 0 for k in sizes}
    graph_idx = 0

    for cache_file in tqdm(cache_files, desc="  pass-2 fill", unit="class"):
        if not cache_file.exists():
            continue
        cls_graphs: list[Data] = torch.load(cache_file, weights_only=False)
        for g in cls_graphs:
            for key in sizes:
                val = g[key]
                if val is None or not isinstance(val, torch.Tensor):
                    continue
                cat_dim = cat_dims[key]
                sz = val.shape[cat_dim]
                off = write_offs[key]
                slc: list = [slice(None)] * val.dim()
                slc[cat_dim] = slice(off, off + sz)
                out_data[key][tuple(slc)] = val
                write_offs[key] += sz
            graph_idx += 1
        del cls_graphs
        gc.collect()

    logger.info(f"  Saving {n_graphs} graphs → {out_path}")
    torch.save((out_data, slices, class_names, raw_funcs), out_path)
    logger.info("  Saved.")
    return n_graphs


class CodeBERTGraphDataset(Dataset):
    """
    Dataset of CPG graphs with CodeBERT node embeddings (773D per node, 7D edge features).

    Parameters
    ----------
    root : str | Path
        Root data directory.
    max_nodes : int
        Graphs with more nodes than this are skipped.
    embedder_device : str
        Device used by CodeBERT during preprocessing.
    mode : str
        "binary", "multiclass", or "group".
    source : str
        Subdirectory under raw/ (e.g. "bigvul").
    pretrained_lm : str
        HuggingFace model ID for node embeddings.
    add_func_tokens : bool
        Tokenize full function text and store in Data.
    top_cwe : int
        Keep only top-N CWE classes by vocab index (0 = all).
    cwe_list : list[str] | None
        Explicit CWE whitelist for vulnerable samples.
    cwe_groups : list[str] | None
        Group whitelist — expanded to all CWEs in those groups.
    max_per_class : int
        Maximum graphs per work-unit (class/CWE/group bucket). 0 = no limit.
        Applied before embedding so only sampled graphs are processed.
        Different values produce separate cached .pt files.
    resample_seed : int
        Random seed for max_per_class sampling. Change to get a different sample.
    """

    def __init__(
        self,
        root: str | Path = "data",
        max_nodes: int = 500,
        embedder_device: str = "cpu",
        use_flash_attention: bool = False,
        embedder_use_amp: bool = True,
        mode: str = "binary",
        source: str = "bigvul",
        pretrained_lm: str = "microsoft/codebert-base",
        func_lm: str = "",
        add_func_tokens: bool = False,
        func_lm_source: str = "raw",   # "raw" | "normalized" — source text for func_lm tokenization
        top_cwe: int = 0,
        cwe_list: list[str] | None = None,
        cwe_groups: list[str] | None = None,
        filter_owasp: bool = False,
        filter_top25_dangerous: bool = False,
        max_per_class: int = 0,
        resample_seed: int = 42,
        func_max_length: int = 512,
        force_rebuild: bool = False,
        storage: str = "inmemory",  # "inmemory" | "lazy"
        transform=None,
        pre_transform=None,
    ):
        self._force_rebuild = force_rebuild
        self._storage = storage  # "inmemory" | "lazy"
        self._func_max_length = func_max_length
        self.max_nodes = max_nodes
        self._embedder_device = embedder_device
        self._use_flash_attention = use_flash_attention
        self._embedder_use_amp = embedder_use_amp
        self._pretrained_lm = pretrained_lm
        self._add_func_tokens = add_func_tokens
        self._func_lm_source = func_lm_source
        self._source = source
        self._top_cwe = top_cwe
        self._cwe_list = list(cwe_list) if cwe_list else []
        self._cwe_groups = cwe_groups
        self._filter_owasp = filter_owasp
        self._filter_top25_dangerous = filter_top25_dangerous
        self._max_per_class = max_per_class
        self._resample_seed = resample_seed
        self._lm_short = pretrained_lm.split("/")[-1]
        self._func_lm = func_lm if func_lm else pretrained_lm
        self._func_short = self._func_lm.split("/")[-1]

        # Load dynamic XML filters — union (no duplicates)
        cwe_dir = Path(root).parent / "data" / "cwe"
        cwe_set = set(self._cwe_list)
        if filter_owasp:
            cwe_set |= _get_cwe_set_from_xml(cwe_dir / "owasptop10.xml")
        if filter_top25_dangerous:
            cwe_set |= _get_cwe_set_from_xml(cwe_dir / "top25.xml")
        self._cwe_list = sorted(cwe_set)  # deterministic order

        # Compute effective CWE filter set (None = no filter)
        self._effective_cwes = _expand_cwe_filter(self._cwe_list if self._cwe_list else None, cwe_groups)
        self._fsuffix = _filter_suffix(self._cwe_list if self._cwe_list else None, cwe_groups, filter_owasp, filter_top25_dangerous)

        source_dir = Path(root) / "raw" / source
        hdf5_check = Path(root) / "graphs" / f"{source}.hdf5"
        has_vocab = (source_dir / "cwe_vocab.json").exists() or hdf5_check.exists()
        if mode in ("multiclass", "group", "owasp") and not has_vocab:
            raise RuntimeError(
                f"mode='{mode}' but cwe_vocab.json not found under {source_dir}. "
                "Run prepare_dataset.py --top-cwe N to generate it."
            )
        self._mode = mode

        super().__init__(str(root), transform, pre_transform)

        _loaded = torch.load(self.processed_paths[0], weights_only=False)
        self._n_graphs = _loaded["n_graphs"]
        self.class_names = _loaded.get("class_names")
        self._graphs: list | None = _loaded.get("graphs") if self._storage == "inmemory" else None
        self.raw_funcs = None  # access per-sample via dataset[i].raw_func

    # ------------------------------------------------------------------
    # PyG hooks
    # ------------------------------------------------------------------

    @property
    def _ds_name(self) -> str:
        ft_suffix = "_ft" if self._add_func_tokens else ""
        ml_suffix = f"_ml{self._func_max_length}" if self._add_func_tokens and self._func_max_length != 512 else ""
        top_suffix = f"_top{self._top_cwe}" if self._top_cwe > 0 else ""
        samp_suffix = (
            f"_s{self._max_per_class}r{self._resample_seed}"
            if self._max_per_class > 0 else ""
        )
        live_suffix = f"_live_{self._func_short}" if self._func_short != self._lm_short else ""
        return (
            f"lm_dataset_{self._source}_{self._mode}_{self._lm_short}{live_suffix}"
            f"{ft_suffix}{ml_suffix}{top_suffix}{self._fsuffix}{samp_suffix}"
        )

    @property
    def _graphs_dir(self) -> Path:
        return Path(self.processed_dir) / f"{self._ds_name}_graphs"

    @property
    def raw_file_names(self) -> list[str]:
        return []

    @property
    def processed_file_names(self) -> list[str]:
        if self._storage == "inmemory":
            return [f"{self._ds_name}.pt"]
        return [f"{self._ds_name}_meta.pt"]

    def download(self) -> None:
        pass

    # ------------------------------------------------------------------
    # Patch fast-path: re-tokenize existing .pt when only func_lm changed
    # ------------------------------------------------------------------

    def _try_patch_from_existing(self, out_path: Path) -> bool:
        """
        Find a compatible dataset (same node-LM + data params, any func_lm or func_max_length),
        re-tokenize func tokens with new func_lm/func_max_length. Skips node embedding entirely.

        Supports both storage formats as source and target:
          inmemory  — single .pt with {"n_graphs", "class_names", "graphs"}
          lazy      — _meta.pt + _graphs/{i}.pt directory

        Returns True if patched, False if no usable base found.
        """
        if not self._add_func_tokens or self._force_rebuild:
            return False

        ft_suffix = "_ft"
        top_suffix = f"_top{self._top_cwe}" if self._top_cwe > 0 else ""
        samp_suffix = (
            f"_s{self._max_per_class}r{self._resample_seed}"
            if self._max_per_class > 0 else ""
        )
        # end_tail: suffix after any ml_suffix — same filter hash + sample params
        # top_suffix matched as substring to handle ml_suffix between ft and top
        end_tail = f"{self._fsuffix}{samp_suffix}"
        prefix = f"lm_dataset_{self._source}_{self._mode}_{self._lm_short}"
        processed_dir = out_path.parent

        def _is_compat(p: Path, meta_ext: str) -> bool:
            stem = p.stem if not meta_ext else p.name[:-len(meta_ext)]
            return (
                p != out_path
                and p.name.endswith(f"{end_tail}{meta_ext}")
                and ft_suffix in stem
                and top_suffix in stem
            )

        logger.info(f"Patch scan: prefix={prefix!r} end_tail={end_tail!r}")

        # Lazy candidates: *_meta.pt with matching tail
        lazy_candidates = [
            p for p in processed_dir.glob(f"{prefix}*_meta.pt")
            if _is_compat(p, "_meta.pt")
        ]
        # Inmemory candidates: *.pt (not _meta.pt) with matching tail
        inmem_candidates = [
            p for p in processed_dir.glob(f"{prefix}*.pt")
            if not p.name.endswith("_meta.pt") and _is_compat(p, ".pt")
        ]
        logger.info(f"  Lazy candidates: {[p.name for p in lazy_candidates]}")
        logger.info(f"  Inmem candidates: {[p.name for p in inmem_candidates]}")

        if not lazy_candidates and not inmem_candidates:
            logger.info("  No compatible base — full rebuild")
            return False

        # Locate usable base
        base_graphs_dir: Path | None = None   # set if lazy source
        base_graphs_list: list | None = None  # set if inmem source
        n = 0
        class_names = None

        for cand in lazy_candidates:
            cand_name = cand.stem[:-5]  # strip "_meta"
            cand_graphs_dir = processed_dir / f"{cand_name}_graphs"
            logger.info(f"  Lazy: {cand.name}  graphs_dir_exists={cand_graphs_dir.exists()}")
            if cand_graphs_dir.exists():
                base_meta = torch.load(cand, weights_only=False)
                n = base_meta.get("n_graphs", 0)
                class_names = base_meta.get("class_names")
                if n > 0:
                    base_graphs_dir = cand_graphs_dir
                    break

        if n == 0:
            for cand in inmem_candidates:
                logger.info(f"  Inmem: {cand.name}")
                try:
                    cand_data = torch.load(cand, weights_only=False)
                except Exception as e:
                    logger.warning(f"  Failed to load {cand.name}: {e}")
                    continue
                if not isinstance(cand_data, dict) or "graphs" not in cand_data:
                    logger.info(f"  Not new inmem format (no 'graphs' key) — skip")
                    continue
                n = cand_data.get("n_graphs", 0)
                class_names = cand_data.get("class_names")
                if n > 0:
                    base_graphs_list = cand_data["graphs"]
                    break

        if n == 0:
            # Fallback: old InMemoryDataset tuple (data, slices, class_names, raw_funcs)
            # Include .pt files with same compat tail — inmem check already rejected non-dict ones
            old_candidates = [
                p for p in processed_dir.glob(f"{prefix}*.pt")
                if not p.name.endswith("_meta.pt") and _is_compat(p, ".pt")
            ]
            logger.info(f"  Old-format candidates: {[p.name for p in old_candidates]}")
            for cand in old_candidates:
                try:
                    cand_data = torch.load(cand, weights_only=False)
                except Exception as e:
                    logger.warning(f"  Failed to load {cand.name}: {e}")
                    continue
                if not isinstance(cand_data, tuple) or len(cand_data) < 4:
                    continue
                _data, _slices, _class_names, _raw_funcs = cand_data
                if not isinstance(_slices, dict) or "y" not in _slices:
                    continue
                _n = len(_slices["y"]) - 1
                if _n == 0:
                    continue
                logger.info(f"  Old InMemory format: {cand.name} ({_n} graphs) — extracting")
                # Extract individual graphs via PyG slicing
                extracted: list = []
                for i in range(_n):
                    g = Data()
                    for key in _data.keys():
                        val = _data[key]
                        if not isinstance(val, torch.Tensor):
                            continue
                        cat_dim = _data.__cat_dim__(key, val)
                        sl = _slices[key]
                        slc: list = [slice(None)] * val.dim()
                        slc[cat_dim] = slice(int(sl[i]), int(sl[i + 1]))
                        g[key] = val[tuple(slc)]
                    g.raw_func = _raw_funcs[i] if i < len(_raw_funcs) else ""
                    extracted.append(g)
                n = _n
                class_names = _class_names
                base_graphs_list = extracted
                break

        if n == 0:
            logger.warning("No usable base (n_graphs=0 or missing graphs) — full rebuild")
            return False

        source = "lazy" if base_graphs_dir is not None else "inmem"
        logger.info(f"Patch fast-path: {source} base → {n} graphs, re-tokenizing at max_length={self._func_max_length}")
        tokenizer = _load_tokenizer(self._func_lm)
        batch_sz = 64

        if self._storage == "inmemory":
            all_patched: list = []
            for batch_start in tqdm(range(0, n, batch_sz), desc="  patch tokenize", unit="batch"):
                batch_end = min(batch_start + batch_sz, n)
                if base_graphs_dir is not None:
                    graphs = [torch.load(base_graphs_dir / f"{i}.pt", weights_only=False)
                              for i in range(batch_start, batch_end)]
                else:
                    graphs = base_graphs_list[batch_start:batch_end]
                batch_funcs = [getattr(g, "raw_func", "") or "" for g in graphs]
                enc = tokenizer(
                    batch_funcs, max_length=self._func_max_length, truncation=True,
                    padding="max_length", return_tensors="pt",
                )
                for j, g in enumerate(graphs):
                    g.func_input_ids = enc["input_ids"][j].unsqueeze(0)
                    g.func_attention_mask = enc["attention_mask"][j].unsqueeze(0)
                    all_patched.append(g)
            torch.save({"n_graphs": n, "class_names": class_names, "graphs": all_patched}, out_path)

        else:  # lazy
            self._graphs_dir.mkdir(parents=True, exist_ok=True)
            for batch_start in tqdm(range(0, n, batch_sz), desc="  patch tokenize", unit="batch"):
                batch_end = min(batch_start + batch_sz, n)
                if base_graphs_dir is not None:
                    graphs = [torch.load(base_graphs_dir / f"{i}.pt", weights_only=False)
                              for i in range(batch_start, batch_end)]
                else:
                    graphs = base_graphs_list[batch_start:batch_end]
                batch_funcs = [getattr(g, "raw_func", "") or "" for g in graphs]
                enc = tokenizer(
                    batch_funcs, max_length=self._func_max_length, truncation=True,
                    padding="max_length", return_tensors="pt",
                )
                for j, g in enumerate(graphs):
                    g.func_input_ids = enc["input_ids"][j].unsqueeze(0)
                    g.func_attention_mask = enc["attention_mask"][j].unsqueeze(0)
                    torch.save(g, self._graphs_dir / f"{batch_start + j}.pt")
            torch.save({"n_graphs": n, "class_names": class_names}, out_path)

        logger.info("  Patch complete.")
        return True

    # ------------------------------------------------------------------
    # Node-embedding cache fast-path: reuse embeddings via parquet_id
    # ------------------------------------------------------------------

    def _build_parquet_id_lookup(self, out_path: Path) -> "dict[int, Data]":
        """
        Load an existing processed .pt with the same source + node LM and
        return a {parquet_id: Data} lookup so embeddings can be reused when
        only the filter / label scheme changes.

        Only inmemory-format files are supported as source.
        Returns {} if no compatible candidate is found.
        """
        if self._force_rebuild:
            return {}

        processed_dir = out_path.parent
        source = self._source
        lm_short = self._lm_short

        candidates: list[Path] = []
        for p in processed_dir.glob(f"lm_dataset_{source}_*.pt"):
            if p.name.endswith("_meta.pt") or p == out_path:
                continue
            stem = p.stem
            prefix = f"lm_dataset_{source}_"
            if not stem.startswith(prefix):
                continue
            # Format: lm_dataset_{source}_{mode}_{lm_short}[_live_...]...
            rest = stem[len(prefix):]          # "{mode}_{lm_short}..."
            underscore = rest.find("_")
            if underscore < 0:
                continue
            after_mode = rest[underscore + 1:]  # "{lm_short}..."
            if not after_mode.startswith(lm_short):
                continue
            candidates.append(p)

        if not candidates:
            logger.info("Node cache fast-path: no compatible .pt found — will embed from scratch.")
            return {}

        # Prefer largest file (most graphs = richest superset)
        candidates.sort(key=lambda p: p.stat().st_size, reverse=True)

        for cand in candidates:
            logger.info(f"Node cache fast-path: loading parquet_id lookup from {cand.name}…")
            try:
                cand_data = torch.load(cand, weights_only=False)
            except Exception as e:
                logger.warning(f"  Failed to load {cand.name}: {e}")
                continue

            lookup: dict[int, Data] = {}

            if isinstance(cand_data, dict) and "graphs" in cand_data:
                for g in cand_data["graphs"]:
                    pid = getattr(g, "parquet_id", None)
                    if pid is not None:
                        pid_val = int(pid.item()) if isinstance(pid, torch.Tensor) else int(pid)
                        if pid_val >= 0:
                            lookup[pid_val] = g

            elif isinstance(cand_data, tuple) and len(cand_data) >= 4:
                _data, _slices, _class_names, _raw_funcs = cand_data
                if isinstance(_slices, dict) and "y" in _slices:
                    n = len(_slices["y"]) - 1
                    for i in range(n):
                        g = Data()
                        for key in _data.keys():
                            val = _data[key]
                            if not isinstance(val, torch.Tensor):
                                continue
                            cat_dim = _data.__cat_dim__(key, val)
                            sl = _slices[key]
                            slc: list = [slice(None)] * val.dim()
                            slc[cat_dim] = slice(int(sl[i]), int(sl[i + 1]))
                            g[key] = val[tuple(slc)]
                        g.raw_func = _raw_funcs[i] if i < len(_raw_funcs) else ""
                        pid = getattr(g, "parquet_id", None)
                        if pid is not None:
                            pid_val = int(pid.item()) if isinstance(pid, torch.Tensor) else int(pid)
                            if pid_val >= 0:
                                lookup[pid_val] = g

            if lookup:
                logger.info(f"  {len(lookup)} graphs in parquet_id lookup.")
                return lookup
            logger.info(f"  No parquet_ids found in {cand.name} — skip.")

        return {}

    # ------------------------------------------------------------------
    # HDF5 source support
    # ------------------------------------------------------------------

    def _build_hdf5_work_units(
        self,
        hdf5_path: Path,
        cwe_vocab: dict,
        is_multi: bool,
        _orig_cwe_vocab: dict,
        id_to_cwe: dict,
    ) -> list[tuple[str, list]]:
        """
        Build work_units from an HDF5 file produced by convert_raw_to_hdf5.py.
        Each entry is (cpg_dict, label, flaw_lines, cwe_str, raw_func, row_id).
        Applies the same CWE filtering as the file-based path.
        """
        import h5py

        group_map = OWASP_GROUP_MAP if self._mode == "owasp" else CWE_GROUP_MAP
        work_units: list[tuple[str, list]] = []

        with h5py.File(hdf5_path, "r") as hf:
            for split in ("benign", "vulnerable"):
                if split not in hf:
                    continue
                grp = hf[split]
                is_benign = (split == "benign")

                if is_benign:
                    entries: list = []
                    for key in sorted(grp.keys()):
                        fg = grp[key]
                        attrs = dict(fg.attrs)
                        flaw_lines = json.loads(attrs.get("flaw_lines", "[]"))
                        raw_func = str(attrs.get("raw_func", ""))
                        row_id = int(attrs.get("row_id", -1))
                        cpg = parse_cpg_hdf5_group(fg, self.max_nodes)
                        if cpg is None:
                            continue
                        entries.append((cpg, 0, flaw_lines, "", raw_func, row_id))
                    work_units.append(("benign", entries))
                    logger.info(f"  [HDF5/{split}]: {len(entries)} graphs loaded")

                elif not is_multi:
                    entries = []
                    for key in sorted(grp.keys()):
                        fg = grp[key]
                        attrs = dict(fg.attrs)
                        cwe_str = _parse_cwe_string(str(attrs.get("cwe", "")))
                        if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                            continue
                        flaw_lines = json.loads(attrs.get("flaw_lines", "[]"))
                        raw_func = str(attrs.get("raw_func", ""))
                        row_id = int(attrs.get("row_id", -1))
                        cpg = parse_cpg_hdf5_group(fg, self.max_nodes)
                        if cpg is None:
                            continue
                        entries.append((cpg, 1, flaw_lines, cwe_str, raw_func, row_id))
                    work_units.append(("vulnerable", entries))
                    logger.info(f"  [HDF5/{split}]: {len(entries)} graphs loaded")

                elif self._mode == "multiclass":
                    cwe_class_groups: dict[int, list] = {}
                    for key in sorted(grp.keys()):
                        fg = grp[key]
                        attrs = dict(fg.attrs)
                        cwe_str = _parse_cwe_string(str(attrs.get("cwe", "")))
                        if cwe_str and cwe_str in cwe_vocab:
                            class_id = cwe_vocab[cwe_str]
                        elif self._top_cwe > 0 or self._effective_cwes is not None:
                            class_id = -1
                        else:
                            class_id = int(attrs.get("label", -1))
                        if class_id < 0:
                            continue
                        if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                            continue
                        flaw_lines = json.loads(attrs.get("flaw_lines", "[]"))
                        raw_func = str(attrs.get("raw_func", ""))
                        row_id = int(attrs.get("row_id", -1))
                        cpg = parse_cpg_hdf5_group(fg, self.max_nodes)
                        if cpg is None:
                            continue
                        cwe_class_groups.setdefault(class_id, []).append(
                            (cpg, class_id, flaw_lines, cwe_str, raw_func, row_id)
                        )
                    for class_id, grp_entries in sorted(cwe_class_groups.items()):
                        cwe_name = id_to_cwe.get(class_id, f"class_{class_id}")
                        cache_key = f"vuln_{cwe_name.replace('-', '_')}"
                        work_units.append((cache_key, grp_entries))
                        logger.info(f"    [HDF5] {cwe_name}: {len(grp_entries)} graphs")

                else:
                    # group / owasp mode
                    grp_buckets: dict[str, list] = {}
                    for key in sorted(grp.keys()):
                        fg = grp[key]
                        attrs = dict(fg.attrs)
                        cwe_str = _parse_cwe_string(str(attrs.get("cwe", "")))
                        group_name = group_map.get(cwe_str, "")
                        if not group_name:
                            continue
                        if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                            continue
                        if is_multi and cwe_str not in cwe_vocab:
                            continue
                        flaw_lines = json.loads(attrs.get("flaw_lines", "[]"))
                        raw_func = str(attrs.get("raw_func", ""))
                        row_id = int(attrs.get("row_id", -1))
                        cpg = parse_cpg_hdf5_group(fg, self.max_nodes)
                        if cpg is None:
                            continue
                        grp_buckets.setdefault(group_name, []).append(
                            (cpg, -1, flaw_lines, cwe_str, raw_func, row_id)
                        )
                    for group_name, grp_entries in sorted(grp_buckets.items()):
                        cache_key = f"vuln_{group_name}"
                        work_units.append((cache_key, grp_entries))
                        logger.info(f"    [HDF5] {group_name}: {len(grp_entries)} graphs")

        return work_units

    def process(self) -> None:
        if self._try_patch_from_existing(Path(self.processed_paths[0])):
            return

        # HDF5 source takes priority over raw file tree
        hdf5_path = Path(self.root) / "graphs" / f"{self._source}.hdf5"
        use_hdf5 = hdf5_path.exists()

        source_dir = Path(self.raw_dir) / self._source
        if not use_hdf5 and not source_dir.is_dir():
            raise RuntimeError(
                f"Source directory not found: {source_dir}. "
                f"Run prepare_dataset.py --out-dir data/raw --format {self._source} first."
                f"\nAlternatively, convert to HDF5: "
                f"uv run python scripts/convert_raw_to_hdf5.py --dataset {self._source}"
            )

        if use_hdf5:
            logger.info(f"HDF5 source detected: {hdf5_path} (skipping raw file tree)")
        else:
            vocab_path = source_dir / "cwe_vocab.json"

        class_sources: list[tuple[str, bool, list[Path]]] = []
        if not use_hdf5:
            for cls_name in ("benign", "vulnerable"):
                cls_dir = source_dir / cls_name
                if cls_dir.is_dir():
                    files = sorted(
                        f for f in cls_dir.iterdir()
                        if f.suffix.lower() in (".json", ".xml", ".graphml")
                        and ".meta." not in f.name
                    )
                    class_sources.append((cls_name, cls_name == "benign", files))
                    logger.info(f"Source [{self._source}/{cls_name}]: {len(files)} CPG files")

            if not class_sources:
                raise RuntimeError(
                    f"No CPG files found under {source_dir}. "
                    "Expected benign/ and/or vulnerable/ subdirectories."
                )

        # ------------------------------------------------------------------
        # Load vocabulary
        # ------------------------------------------------------------------
        is_multi = self._mode in ("multiclass", "group", "owasp")
        if is_multi:
            if use_hdf5:
                import h5py as _h5py
                with _h5py.File(hdf5_path, "r") as _hf:
                    _vocab_json = _hf.attrs.get("cwe_vocab", None)
                if _vocab_json is None:
                    # Fall back to adjacent JSON file
                    _adj = hdf5_path.parent / "cwe_vocab.json"
                    if _adj.exists():
                        _vocab_json = _adj.read_text(encoding="utf-8")
                    else:
                        raise RuntimeError(
                            f"cwe_vocab.json not found in HDF5 attrs or alongside {hdf5_path}. "
                            "Re-run convert_raw_to_hdf5.py with a dataset that has cwe_vocab.json."
                        )
                cwe_vocab: dict[str, int] = json.loads(_vocab_json)
            else:
                with open(vocab_path, encoding="utf-8") as f:
                    cwe_vocab: dict[str, int] = json.load(f)

            # Step 1: filter_owasp / filter_top25_dangerous first (XML-based semantic filter)
            # This narrows to only OWASP or MITRE Top25 CWEs before any count-based trimming
            if self._effective_cwes is not None:
                cwe_vocab = {
                    k: v for k, v in cwe_vocab.items()
                    if k == "benign" or k in self._effective_cwes
                }

            # Step 2: top_cwe — take top N by frequency rank WITHIN the filtered set
            # vocab indices are frequency-ranked (lower = more frequent), so v <= top_cwe
            if self._top_cwe > 0:
                cwe_vocab = {k: v for k, v in cwe_vocab.items() if k == "benign" or v <= self._top_cwe}
        else:
            cwe_vocab = {}

        # Reindex to contiguous 0..N-1 — preserves sort order by original index
        # (benign stays 0; ordering of CWE classes unchanged; safe even when already contiguous)
        # _orig_cwe_vocab keeps pre-reindex indices so graph.cwe_id matches cwe_vocab.json
        _orig_cwe_vocab = dict(cwe_vocab)
        if is_multi:
            _sorted = sorted(cwe_vocab.items(), key=lambda kv: kv[1])
            cwe_vocab = {k: new_i for new_i, (k, _) in enumerate(_sorted)}

        id_to_cwe: dict[int, str] = {v: k for k, v in cwe_vocab.items()}

        # ------------------------------------------------------------------
        # Build work units
        # Entry format: (source, label, flaw_lines, cwe_str, raw_func, row_id)
        #   source = Path (raw file) or dict (pre-parsed CPG from HDF5)
        # ------------------------------------------------------------------
        work_units: list[tuple[str, list]] = []

        if use_hdf5:
            work_units = self._build_hdf5_work_units(
                hdf5_path, cwe_vocab, is_multi, _orig_cwe_vocab, id_to_cwe,
            )

        for cls_name, is_benign_dir, cpg_files in ([] if use_hdf5 else class_sources):
            base_label = 0 if is_benign_dir else 1

            if is_benign_dir:
                # Benign: always include, no CWE filter
                entries: list[tuple[Path, int, list[int], str, str, int]] = []
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    flaw_lines: list[int] = []
                    raw_func = ""
                    row_id = -1
                    if meta_path.exists():
                        with open(meta_path) as mf:
                            _m = json.load(mf)
                        flaw_lines = _m.get("flaw_lines", [])
                        raw_func = _m.get("raw_func", "")
                        row_id = _m.get("id", -1)
                    entries.append((gf, base_label, flaw_lines, "", raw_func, row_id))
                work_units.append((cls_name, entries))

            elif not is_multi:
                # Binary vulnerable: may still filter by CWE if cwe_list/cwe_groups given
                entries = []
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    flaw_lines = []
                    cwe_str = ""
                    raw_func = ""
                    row_id = -1
                    if meta_path.exists():
                        with open(meta_path) as mf:
                            meta = json.load(mf)
                        flaw_lines = meta.get("flaw_lines", [])
                        cwe_str = _parse_cwe_string(meta.get("cwe", ""))
                        raw_func = meta.get("raw_func", "")
                        row_id = meta.get("id", -1)
                    if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                        continue
                    entries.append((gf, base_label, flaw_lines, cwe_str, raw_func, row_id))
                work_units.append((cls_name, entries))

            elif self._mode == "multiclass":
                # Group by CWE class_id → one cache file per CWE
                logger.info(f"  [{cls_name}] grouping {len(cpg_files)} files by CWE class…")
                cwe_class_groups: dict[int, list[tuple[Path, int, list[int], str, str]]] = {}
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    if not meta_path.exists():
                        continue
                    with open(meta_path) as mf:
                        meta = json.load(mf)
                    cwe_str = _parse_cwe_string(meta.get("cwe", ""))
                    if cwe_str and cwe_str in cwe_vocab:
                        class_id = cwe_vocab[cwe_str]
                    elif self._top_cwe > 0 or self._effective_cwes is not None:
                        class_id = -1
                    else:
                        class_id = meta.get("class_id", -1)
                    if class_id < 0:
                        continue
                    # Apply effective_cwes filter
                    if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                        continue
                    flaw_lines = meta.get("flaw_lines", [])
                    raw_func = meta.get("raw_func", "")
                    row_id = meta.get("id", -1)
                    cwe_class_groups.setdefault(class_id, []).append(
                        (gf, class_id, flaw_lines, cwe_str, raw_func, row_id)
                    )

                for class_id, grp_entries in sorted(cwe_class_groups.items()):
                    cwe_name = id_to_cwe.get(class_id, f"class_{class_id}")
                    cache_key = f"vuln_{cwe_name.replace('-', '_')}"
                    work_units.append((cache_key, grp_entries))
                    logger.info(f"    {cwe_name}: {len(grp_entries)} files → {cache_key}.pt")

            else:
                # group / owasp mode: group by category → one cache file per category
                group_map = OWASP_GROUP_MAP if self._mode == "owasp" else CWE_GROUP_MAP
                mode_label = "OWASP category" if self._mode == "owasp" else "vulnerability group"
                logger.info(f"  [{cls_name}] grouping {len(cpg_files)} files by {mode_label}…")
                grp_buckets: dict[str, list[tuple[Path, int, list[int], str, str]]] = {}
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    if not meta_path.exists():
                        continue
                    with open(meta_path) as mf:
                        meta = json.load(mf)
                    cwe_str = _parse_cwe_string(meta.get("cwe", ""))
                    group_name = group_map.get(cwe_str, "")
                    if not group_name:
                        continue
                    if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                        continue
                    if is_multi and cwe_str not in cwe_vocab:
                        continue
                    flaw_lines = meta.get("flaw_lines", [])
                    raw_func = meta.get("raw_func", "")
                    row_id = meta.get("id", -1)
                    grp_buckets.setdefault(group_name, []).append(
                        (gf, -1, flaw_lines, cwe_str, raw_func, row_id)
                    )

                for group_name, grp_entries in sorted(grp_buckets.items()):
                    cache_key = f"vuln_{group_name}"
                    work_units.append((cache_key, grp_entries))
                    logger.info(f"    {group_name}: {len(grp_entries)} files → {cache_key}.pt")

        # ------------------------------------------------------------------
        # Build group vocab for "group" mode (contiguous remapping)
        # ------------------------------------------------------------------
        group_remap: dict[str, int] | None = None
        group_class_names: list[str] | None = None
        if self._mode in ("group", "owasp"):
            present_groups = sorted(
                {k.replace("vuln_", "") for k, _ in work_units if k.startswith("vuln_")}
            )
            if self._mode == "owasp":
                # Use fixed OWASP_VOCAB indices (A01-A10) instead of alphabetical
                group_remap = {"benign": 0}
                for g in present_groups:
                    group_remap[g] = OWASP_VOCAB.get(g, len(group_remap))
                group_class_names = ["benign"] + present_groups
                logger.info(f"OWASP mode: {len(group_class_names)} categories: {group_class_names}")
            else:
                # Contiguous: 0=benign, 1..N = groups sorted alphabetically
                group_remap = {"benign": 0}
                for i, g in enumerate(present_groups, start=1):
                    group_remap[g] = i
                group_class_names = ["benign"] + present_groups
                logger.info(f"Group mode: {len(group_class_names)} classes: {group_class_names}")

        # ------------------------------------------------------------------
        # Derive class_names for multiclass mode
        # ------------------------------------------------------------------
        if self._mode == "multiclass":
            class_names: list[str] | None = [
                name for name, _ in sorted(cwe_vocab.items(), key=lambda kv: kv[1])
            ]
            logger.info(
                f"Multi-class mode: {len(class_names)} classes"
                + (f" (filtered to top-{self._top_cwe})" if self._top_cwe > 0 else "")
                + (f" + effective_cwes filter" if self._effective_cwes else "")
            )
        elif self._mode == "group":
            class_names = group_class_names
        else:
            class_names = None
            logger.info("Binary mode.")

        # ------------------------------------------------------------------
        # Apply per-class subsampling
        # ------------------------------------------------------------------
        if self._max_per_class > 0:
            sampled_units = []
            for cache_key, entries in work_units:
                orig = len(entries)
                entries = _subsample(entries, self._max_per_class, self._resample_seed)
                if orig != len(entries):
                    logger.info(
                        f"  [{cache_key}] subsampled {orig} → {len(entries)} "
                        f"(max_per_class={self._max_per_class}, seed={self._resample_seed})"
                    )
                sampled_units.append((cache_key, entries))
            work_units = sampled_units

        # ------------------------------------------------------------------
        # Cache directory
        # ------------------------------------------------------------------
        ft_suffix = "_ft" if self._add_func_tokens else ""
        src_suffix = f"_{self._func_lm_source}" if self._add_func_tokens else ""
        top_suffix = f"_top{self._top_cwe}" if self._top_cwe > 0 else ""
        samp_suffix = (
            f"_s{self._max_per_class}r{self._resample_seed}"
            if self._max_per_class > 0 else ""
        )
        cache_dir = (
            Path(self.processed_dir)
            / f"cls_cache_{self._mode}{ft_suffix}{src_suffix}{top_suffix}{self._fsuffix}{samp_suffix}"
        )
        cache_dir.mkdir(parents=True, exist_ok=True)

        done_keys    = [k for k, _ in work_units if (cache_dir / f"{k}.pt").exists()]
        pending_keys = [k for k, _ in work_units if not (cache_dir / f"{k}.pt").exists()]

        if done_keys:
            logger.info(f"Resuming — {len(done_keys)} unit(s) already cached: {done_keys}")
            logger.info(f"Remaining: {pending_keys}")

        # Build parquet_id lookup to enable node-embedding cache fast-path.
        # Empty dict = no compatible cache found → fall back to full embedding.
        node_lookup: dict[int, Data] = (
            self._build_parquet_id_lookup(Path(self.processed_paths[0]))
            if pending_keys else {}
        )

        # Embedder and tokenizer are lazy-initialized only when a cache miss
        # actually requires full node embedding.
        embedder = None
        tokenizer = None

        # ------------------------------------------------------------------
        # Process each work unit
        # ------------------------------------------------------------------
        for cache_key, entries in work_units:
            cache_file = cache_dir / f"{cache_key}.pt"

            if cache_file.exists():
                logger.info(f"  [{cache_key}] already cached — skipping")
                continue

            # Determine y label for this work unit
            is_benign_unit = cache_key == "benign"
            if is_benign_unit:
                unit_label = 0
                unit_group_name = "benign"
            elif self._mode == "group":
                group_name = cache_key.replace("vuln_", "")
                unit_label = group_remap[group_name]
                unit_group_name = group_name
            else:
                # multiclass: label is per-entry (from entries[i][1])
                unit_label = None
                unit_group_name = None

            # ------------------------------------------------------------------
            # Node cache fast-path: reuse embeddings from existing .pt by parquet_id.
            # Copies all node/edge tensors; re-derives y, cwe_id, group_id from
            # the new config's vocab so labels are always correct.
            # ------------------------------------------------------------------
            hit_graphs: list[Data] = []
            miss_entries: list = []

            if node_lookup:
                for gf, label, flaw_lines, cwe_str, raw_func, row_id in entries:
                    if row_id >= 0 and row_id in node_lookup:
                        cached_g = node_lookup[row_id]
                        actual_label = unit_label if unit_label is not None else label
                        g = Data()
                        for key in cached_g.keys():
                            if key not in ("y", "cwe_id", "group_id",
                                           "func_input_ids", "func_attention_mask"):
                                g[key] = cached_g[key]
                        g.y = torch.tensor([actual_label], dtype=torch.long)
                        _rcwe = _orig_cwe_vocab.get(cwe_str, -1) if cwe_str else -1
                        g.cwe_id = torch.tensor([_rcwe], dtype=torch.long)
                        _rgrp = CWE_GROUP_MAP.get(cwe_str, "") if cwe_str else ""
                        _rgid = GROUP_VOCAB.get(_rgrp, -1) if _rgrp else (
                            GROUP_VOCAB["benign"] if is_benign_unit else -1
                        )
                        g.group_id = torch.tensor([_rgid], dtype=torch.long)
                        if self.pre_transform is not None:
                            g = self.pre_transform(g)
                        hit_graphs.append(g)
                    else:
                        miss_entries.append((gf, label, flaw_lines, cwe_str, raw_func, row_id))

                if hit_graphs:
                    logger.info(
                        f"  [{cache_key}] node cache: {len(hit_graphs)} hits, "
                        f"{len(miss_entries)} misses"
                    )
            else:
                miss_entries = list(entries)

            # Re-tokenize hit graphs with the current func_lm (fast; no GPU needed)
            if hit_graphs and self._add_func_tokens:
                if tokenizer is None:
                    logger.info(f"Initialising function tokenizer ({self._func_lm})…")
                    tokenizer = _load_tokenizer(self._func_lm)
                _tok_batch = 64
                for _bs in range(0, len(hit_graphs), _tok_batch):
                    _batch = hit_graphs[_bs : _bs + _tok_batch]
                    _funcs = [getattr(_g, "raw_func", "") or "" for _g in _batch]
                    _enc = tokenizer(
                        _funcs, max_length=self._func_max_length, truncation=True,
                        padding="max_length", return_tensors="pt",
                    )
                    for _j, _g in enumerate(_batch):
                        _g.func_input_ids = _enc["input_ids"][_j].unsqueeze(0)
                        _g.func_attention_mask = _enc["attention_mask"][_j].unsqueeze(0)
                        _func_text = _funcs[_j]
                        _g.func_token_lines = _compute_func_token_lines(
                            _func_text, self._func_max_length, tokenizer
                        ).unsqueeze(0)  # [1, max_length]

            if not miss_entries:
                torch.save(hit_graphs, cache_file)
                logger.info(
                    f"  [{cache_key}] done (all from node cache) — {len(hit_graphs)} graphs"
                )
                continue

            # ------------------------------------------------------------------
            # Full pipeline for cache misses (or when no lookup available)
            # ------------------------------------------------------------------
            if embedder is None:
                logger.info(f"Initialising node embedder ({self._pretrained_lm})…")
                embedder = LMNodeEmbedder(
                    model_name=self._pretrained_lm,
                    device=self._embedder_device,
                    use_flash_attention=self._use_flash_attention,
                    use_amp=self._embedder_use_amp,
                )
            if self._add_func_tokens and tokenizer is None:
                logger.info(f"Initialising function tokenizer ({self._func_lm})…")
                tokenizer = _load_tokenizer(self._func_lm)

            # Phase 1: Parse
            logger.info(f"  [{cache_key}] parsing {len(miss_entries)} CPG files…")
            parsed_entries: list[tuple[int, list[int], dict, str, str, int]] = []
            parse_skipped = 0

            for gf, label, flaw_lines, cwe_str, raw_func, row_id in tqdm(
                miss_entries, desc=f"  {cache_key} parse", unit="graph", leave=False
            ):
                if isinstance(gf, dict):
                    cpg = gf  # pre-parsed from HDF5
                else:
                    cpg = parse_cpg(gf, self.max_nodes)
                if cpg is None or not cpg.get("nodes"):
                    parse_skipped += 1
                    continue
                actual_label = unit_label if unit_label is not None else label
                parsed_entries.append((actual_label, flaw_lines, cpg, cwe_str, raw_func, row_id))

            logger.info(
                f"  [{cache_key}] parsed {len(parsed_entries)}/{len(miss_entries)} "
                f"({parse_skipped} skipped — empty or too large)"
            )

            # Phase 2: Embed
            all_codes: list[str] = []
            offsets: list[tuple[int, int]] = []
            for _, _, cpg, _, _, _ in parsed_entries:
                start = len(all_codes)
                all_codes.extend(cpg["codes"])
                offsets.append((start, len(all_codes)))

            total_nodes = len(all_codes)
            embed_batch = 512
            total_batches = (total_nodes + embed_batch - 1) // embed_batch
            logger.info(
                f"  [{cache_key}] embedding {total_nodes} nodes "
                f"across {len(parsed_entries)} graphs ({total_batches} batches of {embed_batch})…"
            )

            cls_parts: list[torch.Tensor] = []
            for i in tqdm(
                range(0, total_nodes, embed_batch),
                desc=f"  {cache_key} embed",
                unit="batch",
                leave=True,
            ):
                cls_parts.append(embedder.embed_batch(all_codes[i : i + embed_batch]))
            all_embeddings = torch.cat(cls_parts, dim=0) if cls_parts else torch.zeros((0, 768))

            # Phase 3: Build
            miss_graphs: list[Data] = []
            for (label, flaw_lines, cpg, cwe_str, raw_func, row_id), (start, end) in zip(parsed_entries, offsets):
                func_input_ids = func_attention_mask = func_token_lines = None
                if tokenizer is not None:
                    if self._func_lm_source == "normalized" and raw_func:
                        from gnn_vuln.data.preprocess import preprocess
                        func_text = preprocess(raw_func, lang="c", normalize=True)
                    elif raw_func:
                        func_text = raw_func   # "raw" — original source, line-number-safe
                    else:
                        func_text = build_func_text(cpg)  # backward compat: old meta without raw_func
                    enc = tokenizer(
                        func_text, max_length=self._func_max_length, truncation=True,
                        padding="max_length", return_tensors="pt",
                    )
                    func_input_ids = enc["input_ids"]
                    func_attention_mask = enc["attention_mask"]
                    func_token_lines = _compute_func_token_lines(
                        func_text, self._func_max_length, tokenizer
                    ).unsqueeze(0)  # [1, max_length]
                graph = build_from_parsed(
                    cpg, all_embeddings[start:end], label, flaw_lines,
                    func_input_ids, func_attention_mask, func_token_lines,
                )
                # Attach CWE metadata — cwe_id uses original vocab index (matches cwe_vocab.json)
                raw_cwe_id = _orig_cwe_vocab.get(cwe_str, -1) if cwe_str else -1
                raw_group_name = CWE_GROUP_MAP.get(cwe_str, "") if cwe_str else ""
                raw_group_id = GROUP_VOCAB.get(raw_group_name, -1) if raw_group_name else (
                    GROUP_VOCAB["benign"] if is_benign_unit else -1
                )
                graph.cwe_id = torch.tensor([raw_cwe_id], dtype=torch.long)
                graph.group_id = torch.tensor([raw_group_id], dtype=torch.long)
                graph.parquet_id = torch.tensor([row_id], dtype=torch.long)
                graph.raw_func = raw_func or ""

                if self.pre_transform is not None:
                    graph = self.pre_transform(graph)
                miss_graphs.append(graph)

            cls_graphs = hit_graphs + miss_graphs
            torch.save(cls_graphs, cache_file)
            logger.info(
                f"  [{cache_key}] done — {len(cls_graphs)} graphs saved to cache"
                + (f" ({len(hit_graphs)} from node cache, {len(miss_graphs)} embedded)"
                   if hit_graphs else "")
            )

        # ------------------------------------------------------------------
        # Distribute per-class caches → final storage format
        # ------------------------------------------------------------------
        global_idx = 0

        if self._storage == "lazy":
            graphs_dir = self._graphs_dir
            graphs_dir.mkdir(parents=True, exist_ok=True)

        all_graphs_buf: list[Data] | None = [] if self._storage == "inmemory" else None

        for cache_key, _ in work_units:
            cache_file = cache_dir / f"{cache_key}.pt"
            if not cache_file.exists():
                logger.warning(f"  Missing cache file: {cache_file} — skipping")
                continue
            cls_graphs: list[Data] = torch.load(cache_file, weights_only=False)
            for g in cls_graphs:
                if self._storage == "lazy":
                    torch.save(g, graphs_dir / f"{global_idx}.pt")
                else:
                    all_graphs_buf.append(g)
                global_idx += 1
            del cls_graphs
            gc.collect()

        if self._storage == "inmemory":
            torch.save(
                {"n_graphs": global_idx, "class_names": class_names, "graphs": all_graphs_buf},
                self.processed_paths[0],
            )
        else:
            torch.save(
                {"n_graphs": global_idx, "class_names": class_names},
                self.processed_paths[0],
            )
        shutil.rmtree(cache_dir, ignore_errors=True)
        logger.info(f"Per-class cache cleaned up. Total graphs: {global_idx}")

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    def len(self) -> int:
        return self._n_graphs

    def get(self, idx: int) -> Data:
        if self._storage == "inmemory":
            return self._graphs[idx]
        return torch.load(self._graphs_dir / f"{idx}.pt", weights_only=False)

    @property
    def num_classes(self) -> int:
        if self.class_names:
            return len(self.class_names)
        return 2

    def get_splits(
        self,
        train_ratio: float = 0.7,
        val_ratio: float = 0.15,
        seed: int = 42,
    ) -> tuple[list[int], list[int], list[int]]:
        """Return (train_idx, val_idx, test_idx) index lists."""
        import random

        indices = list(range(len(self)))
        random.seed(seed)
        random.shuffle(indices)
        n = len(indices)
        t = int(n * train_ratio)
        v = int(n * val_ratio)
        return indices[:t], indices[t : t + v], indices[t + v :]
