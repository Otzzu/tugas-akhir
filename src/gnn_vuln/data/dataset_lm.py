# Reverse: group name → all CWEs in that group
_GROUP_TO_CWES: dict[str, list[str]] = {}
for _cwe, _grp in CWE_GROUP_MAP.items():
    _GROUP_TO_CWES.setdefault(_grp, []).append(_cwe)


def _expand_cwe_filter(
    cwe_list: list[str] | None,
    cwe_groups: list[str] | None,
) -> set[str] | None:
    """Return effective CWE whitelist, or None if no filter."""
    if not cwe_list and not cwe_groups:
        return None
    effective: set[str] = set()
    if cwe_list:
        effective.update(cwe_list)
    if cwe_groups:
        for grp in cwe_groups:
            if grp not in _GROUP_TO_CWES:
                raise ValueError(
                    f"Unknown group '{grp}'. Valid groups: {sorted(GROUP_VOCAB)}"
                )
            effective.update(_GROUP_TO_CWES[grp])
    return effective


def _filter_suffix(cwe_list: list[str] | None, cwe_groups: list[str] | None) -> str:
    if not cwe_list and not cwe_groups:
        return ""
    key = json.dumps(
        {"l": sorted(cwe_list or []), "g": sorted(cwe_groups or [])},
        sort_keys=True,
    )
    return "_f" + hashlib.md5(key.encode()).hexdigest()[:8]


def _subsample(entries: list, max_per_class: int, seed: int) -> list:
    """Return at most max_per_class entries, sampled reproducibly."""
    if max_per_class <= 0 or len(entries) <= max_per_class:
        return entries
    rng = random.Random(seed)
    return rng.sample(entries, max_per_class)


class CodeBERTGraphDataset(InMemoryDataset):
    """
    Dataset of CPG graphs with CodeBERT node embeddings (773D per node, 7D edge features).

    Parameters
    ----------
    root : str | Path
        Root data directory.
    max_nodes : int
        Graphs with more nodes than this are skipped.
    embedder_device : str
        Device used by CodeBERT during preprocessing.
    mode : str
        "binary", "multiclass", or "group".
    source : str
        Subdirectory under raw/ (e.g. "bigvul").
    pretrained_lm : str
        HuggingFace model ID for node embeddings.
    add_func_tokens : bool
        Tokenize full function text and store in Data.
    top_cwe : int
        Keep only top-N CWE classes by vocab index (0 = all).
    cwe_list : list[str] | None
        Explicit CWE whitelist for vulnerable samples.
    cwe_groups : list[str] | None
        Group whitelist — expanded to all CWEs in those groups.
    max_per_class : int
        Maximum graphs per work-unit (class/CWE/group bucket). 0 = no limit.
        Applied before embedding so only sampled graphs are processed.
        Different values produce separate cached .pt files.
    resample_seed : int
        Random seed for max_per_class sampling. Change to get a different sample.
    """

    def __init__(
        self,
        root: str | Path = "data",
        max_nodes: int = 500,
        embedder_device: str = "cpu",
        mode: str = "binary",
        source: str = "bigvul",
        pretrained_lm: str = "microsoft/codebert-base",
        add_func_tokens: bool = False,
        top_cwe: int = 0,
        cwe_list: list[str] | None = None,
        cwe_groups: list[str] | None = None,
        max_per_class: int = 0,
        resample_seed: int = 42,
        transform=None,
        pre_transform=None,
    ):
        self.max_nodes = max_nodes
        self._embedder_device = embedder_device
        self._pretrained_lm = pretrained_lm
        self._add_func_tokens = add_func_tokens
        self._source = source
        self._top_cwe = top_cwe
        self._cwe_list = cwe_list
        self._cwe_groups = cwe_groups
        self._max_per_class = max_per_class
        self._resample_seed = resample_seed
        self._lm_short = pretrained_lm.split("/")[-1]

        # Compute effective CWE filter set (None = no filter)
        self._effective_cwes = _expand_cwe_filter(cwe_list, cwe_groups)
        self._fsuffix = _filter_suffix(cwe_list, cwe_groups)

        source_dir = Path(root) / "raw" / source
        has_vocab = (source_dir / "cwe_vocab.json").exists()
        if mode in ("multiclass", "group") and not has_vocab:
            raise RuntimeError(
                f"mode='{mode}' but cwe_vocab.json not found under {source_dir}. "
                "Run prepare_dataset.py --top-cwe N to generate it."
            )
        self._mode = mode

        super().__init__(str(root), transform, pre_transform)

        result = torch.load(self.processed_paths[0], weights_only=False)
        if len(result) == 3:
            self.data, self.slices, self.class_names = result
        else:
            self.data, self.slices = result
            self.class_names = None

    # ------------------------------------------------------------------
    # PyG hooks
    # ------------------------------------------------------------------

    @property
    def raw_file_names(self) -> list[str]:
        return []

    @property
    def processed_file_names(self) -> list[str]:
        ft_suffix = "_ft" if self._add_func_tokens else ""
        top_suffix = f"_top{self._top_cwe}" if self._top_cwe > 0 else ""
        samp_suffix = (
            f"_s{self._max_per_class}r{self._resample_seed}"
            if self._max_per_class > 0 else ""
        )
        return [
            f"lm_dataset_{self._source}_{self._mode}_{self._lm_short}"
            f"{ft_suffix}{top_suffix}{self._fsuffix}{samp_suffix}.pt"
        ]

    def download(self) -> None:
        pass

    def process(self) -> None:
        source_dir = Path(self.raw_dir) / self._source
        if not source_dir.is_dir():
            raise RuntimeError(
                f"Source directory not found: {source_dir}. "
                f"Run prepare_dataset.py --out-dir data/raw --format {self._source} first."
            )

        vocab_path = source_dir / "cwe_vocab.json"
        class_sources: list[tuple[str, bool, list[Path]]] = []
        for cls_name in ("benign", "vulnerable"):
            cls_dir = source_dir / cls_name
            if cls_dir.is_dir():
                files = sorted(
                    f for f in cls_dir.iterdir()
                    if f.suffix.lower() in (".json", ".xml", ".graphml")
                    and ".meta." not in f.name
                )
                class_sources.append((cls_name, cls_name == "benign", files))
                logger.info(f"Source [{self._source}/{cls_name}]: {len(files)} CPG files")

        if not class_sources:
            raise RuntimeError(
                f"No CPG files found under {source_dir}. "
                "Expected benign/ and/or vulnerable/ subdirectories."
            )

        # ------------------------------------------------------------------
        # Load vocabulary
        # ------------------------------------------------------------------
        is_multi = self._mode in ("multiclass", "group")
        if is_multi:
            with open(vocab_path, encoding="utf-8") as f:
                cwe_vocab: dict[str, int] = json.load(f)
            if self._top_cwe > 0:
                cwe_vocab = {k: v for k, v in cwe_vocab.items() if v <= self._top_cwe}
        else:
            cwe_vocab = {}

        # Apply cwe_list/cwe_groups filter to vocab (for multiclass/group)
        if is_multi and self._effective_cwes is not None:
            cwe_vocab = {
                k: v for k, v in cwe_vocab.items()
                if k == "benign" or k in self._effective_cwes
            }

        id_to_cwe: dict[int, str] = {v: k for k, v in cwe_vocab.items()}

        # ------------------------------------------------------------------
        # Build work units
        # Entry format: (path, label, flaw_lines, cwe_str)
        # ------------------------------------------------------------------
        # work_units: list of (cache_key, entries_list)
        work_units: list[tuple[str, list[tuple[Path, int, list[int], str]]]] = []

        for cls_name, is_benign_dir, cpg_files in class_sources:
            base_label = 0 if is_benign_dir else 1

            if is_benign_dir:
                # Benign: always include, no CWE filter
                entries: list[tuple[Path, int, list[int], str]] = []
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    flaw_lines: list[int] = []
                    if meta_path.exists():
                        with open(meta_path) as mf:
                            flaw_lines = json.load(mf).get("flaw_lines", [])
                    entries.append((gf, base_label, flaw_lines, ""))
                work_units.append((cls_name, entries))

            elif not is_multi:
                # Binary vulnerable: may still filter by CWE if cwe_list/cwe_groups given
                entries = []
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    flaw_lines = []
                    cwe_str = ""
                    if meta_path.exists():
                        with open(meta_path) as mf:
                            meta = json.load(mf)
                        flaw_lines = meta.get("flaw_lines", [])
                        cwe_str = meta.get("cwe", "")
                    if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                        continue
                    entries.append((gf, base_label, flaw_lines, cwe_str))
                work_units.append((cls_name, entries))

            elif self._mode == "multiclass":
                # Group by CWE class_id → one cache file per CWE
                logger.info(f"  [{cls_name}] grouping {len(cpg_files)} files by CWE class…")
                cwe_class_groups: dict[int, list[tuple[Path, int, list[int], str]]] = {}
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    if not meta_path.exists():
                        continue
                    with open(meta_path) as mf:
                        meta = json.load(mf)
                    cwe_str = meta.get("cwe", "")
                    if cwe_str and cwe_str in cwe_vocab:
                        class_id = cwe_vocab[cwe_str]
                    elif self._top_cwe > 0 or self._effective_cwes is not None:
                        class_id = -1
                    else:
                        class_id = meta.get("class_id", -1)
                    if class_id < 0:
                        continue
                    # Apply effective_cwes filter
                    if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                        continue
                    flaw_lines = meta.get("flaw_lines", [])
                    cwe_class_groups.setdefault(class_id, []).append(
                        (gf, class_id, flaw_lines, cwe_str)
                    )

                for class_id, grp_entries in sorted(cwe_class_groups.items()):
                    cwe_name = id_to_cwe.get(class_id, f"class_{class_id}")
                    cache_key = f"vuln_{cwe_name.replace('-', '_')}"
                    work_units.append((cache_key, grp_entries))
                    logger.info(f"    {cwe_name}: {len(grp_entries)} files → {cache_key}.pt")

            else:
                # group mode: group by vulnerability group name → one cache file per group
                logger.info(f"  [{cls_name}] grouping {len(cpg_files)} files by group…")
                grp_buckets: dict[str, list[tuple[Path, int, list[int], str]]] = {}
                for gf in cpg_files:
                    meta_path = gf.parent / f"{gf.stem}.meta.json"
                    if not meta_path.exists():
                        continue
                    with open(meta_path) as mf:
                        meta = json.load(mf)
                    cwe_str = meta.get("cwe", "")
                    group_name = CWE_GROUP_MAP.get(cwe_str, "")
                    if not group_name:
                        continue  # skip CWEs not in our group map
                    if self._effective_cwes is not None and cwe_str not in self._effective_cwes:
                        continue
                    if is_multi and cwe_str not in cwe_vocab:
                        continue
                    flaw_lines = meta.get("flaw_lines", [])
                    grp_buckets.setdefault(group_name, []).append(
                        (gf, -1, flaw_lines, cwe_str)  # label assigned later
                    )

                for group_name, grp_entries in sorted(grp_buckets.items()):
                    cache_key = f"vuln_{group_name}"
                    work_units.append((cache_key, grp_entries))
                    logger.info(f"    {group_name}: {len(grp_entries)} files → {cache_key}.pt")

        # ------------------------------------------------------------------
        # Build group vocab for "group" mode (contiguous remapping)
        # ------------------------------------------------------------------
        group_remap: dict[str, int] | None = None
        group_class_names: list[str] | None = None
        if self._mode == "group":
            # Collect groups present in work_units
            present_groups = sorted(
                {k.replace("vuln_", "") for k, _ in work_units if k.startswith("vuln_")}
            )
            # Contiguous: 0=benign, 1..N = groups sorted alphabetically
            group_remap = {"benign": 0}
            for i, g in enumerate(present_groups, start=1):
                group_remap[g] = i
            group_class_names = ["benign"] + present_groups
            logger.info(f"Group mode: {len(group_class_names)} classes: {group_class_names}")

        # ------------------------------------------------------------------
        # Derive class_names for multiclass mode
        # ------------------------------------------------------------------
        if self._mode == "multiclass":
            class_names: list[str] | None = [
                name for name, _ in sorted(cwe_vocab.items(), key=lambda kv: kv[1])
            ]
            logger.info(
                f"Multi-class mode: {len(class_names)} classes"
                + (f" (filtered to top-{self._top_cwe})" if self._top_cwe > 0 else "")
                + (f" + effective_cwes filter" if self._effective_cwes else "")
            )
        elif self._mode == "group":
            class_names = group_class_names
        else:
            class_names = None
            logger.info("Binary mode.")

        # ------------------------------------------------------------------
        # Apply per-class subsampling
        # ------------------------------------------------------------------
        if self._max_per_class > 0:
            sampled_units = []
            for cache_key, entries in work_units:
                orig = len(entries)
                entries = _subsample(entries, self._max_per_class, self._resample_seed)
                if orig != len(entries):
                    logger.info(
                        f"  [{cache_key}] subsampled {orig} → {len(entries)} "
                        f"(max_per_class={self._max_per_class}, seed={self._resample_seed})"
                    )
                sampled_units.append((cache_key, entries))
            work_units = sampled_units

        # ------------------------------------------------------------------
        # Cache directory
        # ------------------------------------------------------------------
        ft_suffix = "_ft" if self._add_func_tokens else ""
        top_suffix = f"_top{self._top_cwe}" if self._top_cwe > 0 else ""
        samp_suffix = (
            f"_s{self._max_per_class}r{self._resample_seed}"
            if self._max_per_class > 0 else ""
        )
        cache_dir = (
            Path(self.processed_dir)
            / f"cls_cache_{self._mode}{ft_suffix}{top_suffix}{self._fsuffix}{samp_suffix}"
        )
        cache_dir.mkdir(parents=True, exist_ok=True)

        done_keys    = [k for k, _ in work_units if (cache_dir / f"{k}.pt").exists()]
        pending_keys = [k for k, _ in work_units if not (cache_dir / f"{k}.pt").exists()]

        if done_keys:
            logger.info(f"Resuming — {len(done_keys)} unit(s) already cached: {done_keys}")
            logger.info(f"Remaining: {pending_keys}")

        embedder = None
        if pending_keys:
            logger.info(f"Initialising node embedder ({self._pretrained_lm})…")
            embedder = CodeBERTNodeEmbedder(
                model_name=self._pretrained_lm,
                device=self._embedder_device,
            )

        tokenizer = None
        if self._add_func_tokens and pending_keys:
            from transformers import AutoTokenizer
            logger.info(f"Initialising function tokenizer ({self._pretrained_lm})…")
            tokenizer = AutoTokenizer.from_pretrained(self._pretrained_lm)

        # ------------------------------------------------------------------
        # Process each work unit
        # ------------------------------------------------------------------
        data_list: list[Data] = []

        for cache_key, entries in work_units:
            cache_file = cache_dir / f"{cache_key}.pt"

            if cache_file.exists():
                cls_graphs: list[Data] = torch.load(cache_file, weights_only=False)
                logger.info(f"  [{cache_key}] loaded {len(cls_graphs)} graphs from cache")
                data_list.extend(cls_graphs)
                continue

            # Determine y label for this work unit
            is_benign_unit = cache_key == "benign"
            if is_benign_unit:
                unit_label = 0
                unit_group_name = "benign"
            elif self._mode == "group":
                group_name = cache_key.replace("vuln_", "")
                unit_label = group_remap[group_name]
                unit_group_name = group_name
            else:
                # multiclass: label is per-entry (from entries[i][1])
                unit_label = None
                unit_group_name = None

            # Phase 1: Parse
            logger.info(f"  [{cache_key}] parsing {len(entries)} CPG files…")
            parsed_entries: list[tuple[int, list[int], dict, str]] = []
            parse_skipped = 0

            for gf, label, flaw_lines, cwe_str in tqdm(
                entries, desc=f"  {cache_key} parse", unit="graph", leave=False
            ):
                cpg = parse_cpg(gf, self.max_nodes)
                if cpg is None:
                    parse_skipped += 1
                    continue
                actual_label = unit_label if unit_label is not None else label
                parsed_entries.append((actual_label, flaw_lines, cpg, cwe_str))

            logger.info(
                f"  [{cache_key}] parsed {len(parsed_entries)}/{len(entries)} "
                f"({parse_skipped} skipped — empty or too large)"
            )

            # Phase 2: Embed
            all_codes: list[str] = []
            offsets: list[tuple[int, int]] = []
            for _, _, cpg, _ in parsed_entries:
                start = len(all_codes)
                all_codes.extend(cpg["codes"])
                offsets.append((start, len(all_codes)))

            total_nodes = len(all_codes)
            embed_batch = 512
            total_batches = (total_nodes + embed_batch - 1) // embed_batch
            logger.info(
                f"  [{cache_key}] embedding {total_nodes} nodes "
                f"across {len(parsed_entries)} graphs ({total_batches} batches of {embed_batch})…"
            )

            cls_parts: list[torch.Tensor] = []
            for i in tqdm(
                range(0, total_nodes, embed_batch),
                desc=f"  {cache_key} embed",
                unit="batch",
                leave=True,
            ):
                cls_parts.append(embedder.embed_batch(all_codes[i : i + embed_batch]))
            all_embeddings = torch.cat(cls_parts, dim=0) if cls_parts else torch.zeros((0, 768))

            # Phase 3: Build
            cls_graphs = []
            for (label, flaw_lines, cpg, cwe_str), (start, end) in zip(parsed_entries, offsets):
                func_input_ids = func_attention_mask = None
                if tokenizer is not None:
                    func_text = build_func_text(cpg)
                    enc = tokenizer(
                        func_text, max_length=512, truncation=True,
                        padding="max_length", return_tensors="pt",
                    )
                    func_input_ids = enc["input_ids"]
                    func_attention_mask = enc["attention_mask"]
                graph = build_from_parsed(
                    cpg, all_embeddings[start:end], label, flaw_lines,
                    func_input_ids, func_attention_mask,
                )
                # Attach CWE metadata
                raw_cwe_id = cwe_vocab.get(cwe_str, -1) if cwe_str else -1
                raw_group_name = CWE_GROUP_MAP.get(cwe_str, "") if cwe_str else ""
                raw_group_id = GROUP_VOCAB.get(raw_group_name, -1) if raw_group_name else (
                    GROUP_VOCAB["benign"] if is_benign_unit else -1
                )
                graph.cwe_id = torch.tensor([raw_cwe_id], dtype=torch.long)
                graph.group_id = torch.tensor([raw_group_id], dtype=torch.long)

                if self.pre_transform is not None:
                    graph = self.pre_transform(graph)
                cls_graphs.append(graph)

            torch.save(cls_graphs, cache_file)
            logger.info(f"  [{cache_key}] done — {len(cls_graphs)} graphs saved to cache")
            data_list.extend(cls_graphs)

        if not data_list:
            raise RuntimeError(
                "No graphs were built. Check that your JSON files match the Joern export format."
            )

        data, slices = self.collate(data_list)
        torch.save((data, slices, class_names), self.processed_paths[0])
        logger.info(f"Saved {len(data_list)} graphs → {self.processed_paths[0]}")

        shutil.rmtree(cache_dir, ignore_errors=True)
        logger.info("Per-class cache cleaned up.")

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    @property
    def num_classes(self) -> int:
        if self.class_names:
            return len(self.class_names)
        return int(self.data.y.max().item()) + 1

    def get_splits(
        self,
        train_ratio: float = 0.7,
        val_ratio: float = 0.15,
        seed: int = 42,
    ) -> tuple[list[int], list[int], list[int]]:
        """Return (train_idx, val_idx, test_idx) index lists."""
        import random

        indices = list(range(len(self)))
        random.seed(seed)
        random.shuffle(indices)
        n = len(indices)
        t = int(n * train_ratio)
        v = int(n * val_ratio)
        return indices[:t], indices[t : t + v], indices[t + v :]
